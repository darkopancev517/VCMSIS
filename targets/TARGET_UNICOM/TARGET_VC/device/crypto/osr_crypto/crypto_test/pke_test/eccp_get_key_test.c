extern uint32_t get_systick_micros(void);


uint32_t eccp_get_key_test_internal(const eccp_curve_t *curve, char *curve_name, uint8_t *prikey, uint8_t *pubkey, uint32_t ret_value, char *info)
{
	uint8_t buf[2*ECCP_MAX_BYTE_LEN+1]={0};
	uint32_t byteLen = GET_BYTE_LEN(curve->eccp_p_bitLen);
	uint32_t ret;

    uint32_t start = get_systick_micros();
	ret = eccp_get_pubkey_from_prikey((eccp_curve_t *)curve, prikey, buf);
    uint32_t duration = get_systick_micros() - start;

	if((ret_value != ret))
	{
		printf("\r\n ECCp %s get key %s failure 1, ret=%lu", curve_name, info, ret);
		return 1;
	}
	if((PKE_SUCCESS == ret) && memcmp_(buf, pubkey, 2*byteLen))
	{
		printf("\r\n ECCp %s get key %s failure 2, ret=%lu", curve_name, info, ret);
		print_buf_U8(prikey, byteLen, "prikey");
		print_buf_U8(buf, 2*byteLen, "pubkey");
		print_buf_U8(pubkey, 2*byteLen, "std pubkey");
		return 1;
	}

	printf("\r\n ECCp %s get key %s success - duration: %lu us", curve_name, info, duration);

	return 0;
}





uint32_t eccp_get_key_test(const eccp_curve_t *curve, char *curve_name, uint8_t *std_prikey, uint8_t *std_pubkey,
		uint8_t *pubkey_1, uint8_t *pubkey_2, uint8_t *pubkey_n_minus_2, uint8_t *pubkey_n_minus_1)
{
	//standard private key and public key
	uint8_t prikey[ECCP_MAX_BYTE_LEN]={0};
	uint32_t nByteLen = GET_BYTE_LEN(curve->eccp_n_bitLen);

	printf("\r\n -------------- %s get key test -------------- ", curve_name);

	/********** eccp get key standard data test **********/
	if(eccp_get_key_test_internal(curve, curve_name, std_prikey, std_pubkey, PKE_SUCCESS, "standard data test"))
		return 1;

	/********** prikey is 0 **********/
	memset_(prikey, 0, nByteLen);
	if(eccp_get_key_test_internal(curve, curve_name, prikey, NULL, PKE_ZERO_ALL, "test(prikey=0)"))
		return 1;

	/********** prikey is 1 **********/
	memset_(prikey, 0, nByteLen);
	prikey[nByteLen-1]=1;
	if(eccp_get_key_test_internal(curve, curve_name, prikey, pubkey_1, PKE_SUCCESS, "test(prikey=1)"))
		return 1;

	/********** prikey is 2 **********/
	memset_(prikey, 0, nByteLen);
	prikey[nByteLen-1]=2;
	if(eccp_get_key_test_internal(curve, curve_name, prikey, pubkey_2, PKE_SUCCESS, "test(prikey=2)"))
		return 1;

	/********** prikey is n-2 **********/
	reverse_byte_array((uint8_t *)(curve->eccp_n), prikey, nByteLen);
	prikey[nByteLen-1]-=2;
	if(eccp_get_key_test_internal(curve, curve_name, prikey, pubkey_n_minus_2, PKE_SUCCESS, "test(prikey=n-2)"))
		return 1;

	/********** prikey is n-1 **********/
	reverse_byte_array((uint8_t *)(curve->eccp_n), prikey, nByteLen);
	prikey[nByteLen-1]-=1;
	if(eccp_get_key_test_internal(curve, curve_name, prikey, pubkey_n_minus_1, PKE_SUCCESS, "test(prikey=n-1)"))
		return 1;

	/********** prikey is n **********/
	reverse_byte_array((uint8_t *)(curve->eccp_n), prikey, nByteLen);
	if(eccp_get_key_test_internal(curve, curve_name, prikey, NULL, PKE_INTEGER_TOO_BIG, "test(prikey=n)"))
		return 1;

	/********** prikey is n+1 **********/
	reverse_byte_array((uint8_t *)(curve->eccp_n), prikey, nByteLen);
	prikey[nByteLen-1]+=1;
	if(eccp_get_key_test_internal(curve, curve_name, prikey, NULL, PKE_INTEGER_TOO_BIG, "test(prikey=n+1)"))
	{
		return 1;
	}
	else
	{
		printf("\r\n");
		return 0;
	}
}



