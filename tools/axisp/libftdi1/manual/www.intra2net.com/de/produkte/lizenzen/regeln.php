<h1>Lizenzierungsregeln f&uuml;r einzelne Funktionsmodule</h1>

<h2>Network Security</h2>
<p>Netzwerklizenz: Jede Lizenz enth&auml;lt das Linux Betriebssystem und die Network
Security Funktion. Sie unterst&uuml;tzt eine unbegrenzte Anzahl an Firewall- und
VPN-Verbindungen f&uuml;r eine beliebige Anzahl an Gegenstellen. Grenzen werden nur
durch die Leistungsf&auml;higkeit der verwendeten Hardware gesetzt.</p>

<h2>Mail Security</h2>
<p>Volumenlizenz: Die Anzahl der zu lizenzierenden Benutzer richtet sich nach der Anzahl
von konfigurierten E-Mail Konten auf dem Zielsystem bei Domain-Weiterleitungen
plus einzelnen E-Mail-Weiterleitungen. E-Mail Verteilerlisten werden nicht als E-Mail
Konten gez&auml;hlt.</p>

<h2>Web Security</h2>
<p>Volumenlizenz: Die Anzahl der zu lizenzierenden Benutzer richtet sich nach der Anzahl
der im System eingerichteten Benutzer sowie, wenn der Proxy ohne Authentifizierung
verwendet wird, nach der Anzahl an verschiedenen IP-Adressen, die gleichzeitig
Verbindungen aufbauen. Die zul&auml;ssige Anzahl der gleichzeitigen Verbindungen
betr&auml;gt maximal das zehnfache der lizenzierten Benutzeranzahl.</p>

<h2>Mail Server und Groupware</h2>
<p>Volumenlizenz: Die Anzahl der zu lizenzierenden Benutzer richtet sich nach der
Anzahl von aktiven E-Mail Konten auf dem Intra2net Mail-Server. Der Intra2net
Groupware Client steht f&uuml;r jeden PC-Arbeitsplatz zur Verf&uuml;gung, der zusammen mit
dem Intra2net Business Server verwendet wird.</p>

<h2>Stand-By Systeme</h2>
<p>Netzwerklizenz: Zur Realisierung eines Cold-Stand-By Systems ben&ouml;tigen Sie eine
zus&auml;tzliche Intra2net Network Security Lizenz f&uuml;r das zweite System. Die gleichzeitige
Installation einer Lizenz auf zwei betriebsbereiten Systemen ist nicht zul&auml;ssig.</p>

