<h1>Beispiele zu den Lizenzierungsregeln</h1>

<p>Die folgenden Beispiele veranschaulichen, wie der jeweilige Lizenzumfang f&uuml;r
unterschiedliche Einsatzszenarien ermittelt wird.</p>

<h2>Beispiel 1: Intra2net Business Server im Unternehmen</h2>
<p>Ein Dienstleistungsunternehmen hat 10 Benutzer mit festen Rechnern. Daneben
werden noch 5 weitere Notebooks von denselben Mitarbeitern verwendet. Zus&auml;tzlich
nutzen die Mitarbeiter die Groupware des Intra2net Business Server von unterwegs.</p>

<p>Ihr Kunde lizenziert 10 Benutzer in der Staffel 10-24 Benutzer:</p>

