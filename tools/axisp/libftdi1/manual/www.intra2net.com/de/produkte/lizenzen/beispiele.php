<tr><td>40</td> <td>I2N-ISG-120</td> <td>Intra2net Security Gateway, 25-49 Benutzer</td></tr>
</table>

<p>In der Praxis kann es bedingt durch den h&ouml;heren Rabatt in der Mengenstaffel
50-99 Benutzer f&uuml;r den Kunden vorteilhaft sein, eine Lizenz f&uuml;r 50 Benutzer
