<tr>
<td style="border-bottom: 1px solid #999;">Anzahl</td> <td style="border-bottom: 1px solid #999;">Artikel</td> <td style="border-bottom: 1px solid #999;">Mengenstaffel</td></tr>
<tr><td>10</td> <td>I2N-IBS-110</td> <td>Intra2net Business Server, 10-24 Benutzer</td></tr>
</table>
<br>

<h2>Beispiel 2: Intra2net Security Gateway zum Schutz eines Exchange Servers</h2>
<p>Ihr Kunde betreibt einen Microsoft Exchange Server und besch&auml;ftigt 170 Mitarbeiter.
100 dieser Mitarbeiter arbeiten in der Produktion und haben keinen Internet Zugriff.
70 Mitarbeiter arbeiten in der Verwaltung und haben einen geregelten Zugriff
auf E-Mails. Der Kunde hat zus&auml;tzlich 20 E-Mail Verteilerlisten f&uuml;r Projektverteiler,
Bewerbungen und Vertriebsanfragen eingerichtet. Verwendet werden die Antivirus
und Antispam Funktionalit&auml;t des Intra2net Security Gateway.</p>

<p>Ihr Kunde lizenziert 70 Benutzer in der Staffel 50-99 Benutzer:</p>

<table cellspacing="0" cellpadding="5" width="100%">
<tr>
<td style="border-bottom: 1px solid #999;">Anzahl</td> <td style="border-bottom: 1px solid #999;">Artikel</td> <td style="border-bottom: 1px solid #999;">Mengenstaffel</td></tr>
<tr><td>70</td> <td>I2N-IBS-130</td> <td>Intra2net Security Gateway, 50-99 Benutzer</td></tr>
</table>
<br>

<h2>Beispiel 3: Intra2net Business Server in einem Hotel</h2>
<p>Ein Wellnesshotel mit 70 Betten hat 5 PC-Arbeitspl&auml;tze in der Verwaltung und bietet
den G&auml;sten kostenlos einen sicheren Internetzugang an. Hierf&uuml;r wird das Web Security
Modul des Intra2net Business Server verwendet, der Proxy wird im transparenten
Modus ohne Authentifizierung eingesetzt. Der Hotelier m&ouml;chte maximal 35 G&auml;sten
einen gleichzeitigen Internetzugriff zur Verf&uuml;gung stellen.</p>

<p>Ihr Kunde lizenziert 40 Benutzer in der Staffel 25-49 Benutzer:</p>

