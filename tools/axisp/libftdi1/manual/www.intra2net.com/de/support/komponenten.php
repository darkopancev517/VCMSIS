<h1><a name="festplattencontroller"></a>Festplatten Controller</h1>
<ul class="listfrench">
<li style="background-color: #f6f6f6; color: #303E58;">Zertifizierte Hardware-RAID Controller
 <ul class="listfrench">
 <li>Hewlett Packard E200</li>
 <li>Hewlett Packard P212</li>
 </ul>
</ul>
<ul class="listfrench">
<li>Integrierte IDE- und SATA-Controller</li>
<li>BIOS-RAID-Controller (SATA) <br>
Es gibt viele verschiedene Controller von unterschiedlichen Herstellern, wir listen daher nicht die Produkte, sondern die Controller-Chips.<br>
<ul class="listfrench">
<li>Alle Chips, die AHCI verwenden (z. B. viele JMicron-Chips)</li>
<li>Promise TX2, TX4, SX4 (nicht aber SX8)</li>
<li>Silicon Image 3112, 3114, 3124</li>
<li>Marvell 88SX50xx, 88SX60xx, 88SX70xx</li>
</ul>
</li>

<li>Hardware-RAID-Controller (SATA, SAS, SCSI) <br>
<ul class="listfrench">
<li>3ware/AMCC 9500S, 9550SX, 9650SE, 9690SA</li>
<li>Adaptec AIC-7000 Serie (u.a. AHA-29xx, AHA-39xx)</li>
<li>Adaptec RAID 3085, 31205, 31605, 3805, 4000, 4800SAS, 4805SAS, 5405Z, 5445Z (aacraid)</li>
<li>Buslogic MultiMaster, MultiMaster NC, FlashPoint (von VMWare verwendet)</li>
<li>Dell PERC 5/i, 5/E, 6/i, 6/E (MegaRAID)</li>
<li>Emulex HBAs: bitte fragen Sie unseren Vertrieb zu detaillierten Spezifikationen</li>
<li>Hewlett Packard SA641, SA642, SA6400, SA6i, P600, P800, P400, P400i, E200, E200i, E500, P212, P410, P411 (cciss)</li>
<li>HighPoint RocketRAID Controller der 3000-Serie</li>
<li>IBM ServeRAID 7t, 7k (ips)</li>
<li>IBM ServeRAID 8i, 8k (aacraid)</li>
<li>ICP Vortex ICP-Controller der Serien 5000 und 9000 (aacraid)</li>
<li>ICP Vortex GDT-Controller der Serien RZ, RS, RN, RD, RP</li>
<li>LSI Logic Fusion MPT Controller SAS 1064, 1064e, 1068, 1068e, 1078, 1078e: von diesen Controllern wird abgeraten</li>
<li>LSI Logic MegaRAID SAS-Controller der Serien 8300, 8400, 8700, 8800 (nicht 8200!)</li>
<li>LSI Logic MegaRAID SCSI-Controller</li>
<li>NCR/Symbios Logic 53C7x, 5380</li>
<li>Promise SuperTrak EX-Serie (EX4350, EX4650, EX8300, EX8350, EX8650, EX8654, EX12350, EX16300, EX16350, EX24350)</li>
<li>Qlogic HBAs: bitte fragen Sie unseren Vertrieb zu detaillierten Spezifikationen</li>
</ul>
</li>
</ul>
<p>Viele Controller bieten kein echtes Hardware-RAID, sondern nur BIOS-RAID. Sie werden in den JBOD-Modus (einzelne Festplatten) geschaltet, RAID kann dann in der Software-Oberfl&auml;che aktiviert werden.</p>
<p>Hardware-RAID-Controller erkennt man typischerweise daran, da&szlig; sie eigenen Cache-Speicher integriert haben.</p>

<h1><a name="festplatten"></a>Festplatten</h1>
<ul class="listfrench">
<li>Mindestens 40 GB</li>
<li>Pro System werden maximal 2 TB Speicherplatz unterst&uuml;tzt</li>
</ul>
<p>SSD / Flash-Speicher: Es finden f&uuml;r Protokoll- und Tempor&auml;rdateien st&auml;ndig Schreibzugriffe statt. Nur hochwertige SSDs sind daf&uuml;r dauerhaft ausgelegt.</p>

<h1><a name="isdn"></a>ISDN Karten</h1>
<ul class="listfrench">
<li style="background-color: #f6f6f6; color: #303E58;">AVM Fritz PCI (zertifiziert)</li>
<li>AVM Fritz USB</li>
<li>AVM B1</li>
<li>AVM C2, C4 (nur ein Bus)</li>
<li>Nur Mehrger&auml;teanschluss</li>
</ul>
<p>In vielen aktuellen Mainboards funktionieren nur noch PCI-Karten mit 3.3&nbsp;Volt.<br>
&Auml;ltere ISDN-Karten ben&ouml;tigen h&auml;ufig 5&nbsp;Volt und k&ouml;nnen nicht mehr verwendet werden.</p>

<h1><a name="usv"></a>USV Systeme</h1>
