APC SmartUPS<br>
(Anbindung &uuml;ber USB und serielles Kabel)<br>
<br>
</li>
<li>APC BackUPS (Anbindung &uuml;ber USB)</li>
<li>Anbindung an andere Rechner &uuml;ber das <a href="http://www.networkupstools.org/">nut-Protokoll</a></li>
</ul>


