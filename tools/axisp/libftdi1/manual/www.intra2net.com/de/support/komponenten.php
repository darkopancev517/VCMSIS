<h1><a name="cpu"></a><a name="prozessoren"></a>Prozessoren</h1>
<ul class="listfrench">
<li>Intel x86-Prozessoren ab Pentium IV, mindestens 2 GHz</li>
<li>AMD x86-Prozessoren, ab K8-Architektur (Athlon), mindestens 2 GHz</li>
<li>Multiprozessor- oder Mehrkern-Systeme werden unterst&uuml;zt</li>
</ul>

<h1><a name="cpu"></a><a name="arbeitsspeicher"></a>Arbeitsspeicher</h1>
<ul class="listfrench">
<li>Intra2net Business Server: mindestens 2 GB RAM</li>
<li>Intra2net Security Gateway: mindestens 1 GB RAM</li>
<li>Intra2net Network Security: mindestens 1 GB RAM</li>
</ul>
<ul class="listfrench">
<li>Unsere Empfehlung bis 10 Benutzer: 2 GB RAM</li>
<li>Unsere Empfehlung bis 50 Benutzer: 4 GB RAM</li>
<li>Unsere Empfehlung f&uuml;r 50 und mehr Benutzer: 8 GB RAM</li>
</ul>
<p>Es werden maximal 16 GB Arbeitsspeicher verwendet. Wir empfehlen Speicherbausteine von Markenherstellern.</p>
</ul>

<h1><a name="mainboards"></a>Mainboards</h1>
<ul class="listfrench">
<li>Intel: alle Chips&auml;tze mit ICH5, ICH6, ICH7, ICH8, ICH9 oder neuer</li>
<li>Nvidia: Serien nForce 400 / nForce 500 / nForce 600 / nForce 700 f&uuml;r Intel und AMD</li>
<li>AMD/ATI: Chips&auml;tze mit SB600 und SB700</li>
<li>VIA: VIA K8M800 / VT8237</li>
</ul>
<p>Eine beliebige Grafikkarte, integriert oder separat, mu&szlig; auf dem Mainboard vorhanden sein.</p>
<p>Von Mainboards mit Chips&auml;tzen der Hersteller SIS, ULI und ALI wird abgeraten.</p>
<p>Von den in den Desktop- u. Workstation-Serien Fujitsu Esprimo und Fujitsu Celsius enthaltenen Mainboards wird abgeraten.</p>

