Email Address []:

C:\makecert&gt;openssl pkcs12 -export -in newcert.cer -inkey privatekey.pem 
-out newcert.p12
Loading 'screen' into random state - done</pre></li><li><p>W&auml;hlen Sie ein Transportpasswort, mit dem die Schl&uuml;sseldatei auf dem Weg zum
          VPN-Client im iPhone gesch&uuml;tzt wird. Das Passwort muss mindestens 4 Zeichen lang
          sein.</p><pre class="programlisting">Enter Export Password:
Verifying password - Enter Export Password:

C:\makecert&gt;del privatekey.pem</pre></li><li><p>Der eigene Schl&uuml;ssel f&uuml;r das iPhone muss nun auf dem PC mit dem iPhone
          Konfigurationsprogramm installiert werden. Dies ist normalerweise &uuml;ber einen Doppelklick auf
          die Datei <code class="filename">newcert.p12</code> im Windows Explorer m&ouml;glich. </p></li><li><p>Es startet der Zertifikatsimport-Assistent. Geben Sie das eben gew&auml;hlte
          Transportpasswort ein und markieren den Schl&uuml;ssel als exportierbar (ansonsten kann er nicht
          auf das iPhone transferiert werden).</p><div class="mediaobject"><img src="../images/vpn/iphone/windows-import-p12-01.png"></div></li><li><p>Lassen Sie den Zertifikatsspeicher automatisch ausw&auml;hlen.</p><div class="mediaobject"><img src="../images/vpn/iphone/windows-import-p12-02.png"></div></li><li><p>Starten Sie nun das iPhone Konfigurationsprogramm und legen ein neues
          Konfigurationsprofil an.</p><div class="mediaobject"><img src="../images/vpn/iphone/iphone-config-profile.png"></div></li><li><p>Wechseln Sie zum Men&uuml; <span class="guimenu">Zertifikate</span> und f&uuml;gen ein neues Zertifikat
          hinzu.</p><div class="mediaobject"><img src="../images/vpn/iphone/iphone-config-cert-own-01.png"></div></li><li><p>Sie bekommen den Inhalt mehrerer Zertifikatsspeicher angezeigt. In der Spalte
            <span class="guilabel">Ausgestellt f&uuml;r</span> sind die Zertifikate nach dem Inhalt des Feldes
            <span class="property">Common Name</span> sortiert. W&auml;hlen Sie das eben erstellte eigene
          Zertifikat aus.</p><div class="mediaobject"><img src="../images/vpn/iphone/iphone-config-cert-own-02.png"></div></li><li><p>Geben Sie das vorhin gew&auml;hlte Transportpasswort ein. </p><div class="mediaobject"><img src="../images/vpn/iphone/iphone-config-cert-own-03.png"></div></li><li><p> Als n&auml;chstes muss das Zertifikat des iPhones auch dem Intranator bekannt gemacht
          werden. &Ouml;ffnen Sie dazu im "makecert"-Programmverzeichnis die Zertifikatsdatei
            (<code class="filename">newcert.cer</code>) mit einem Texteditor (z.B.
          <span class="command"><strong>Wordpad</strong></span>) und &uuml;bernehmen den gesamten Inhalt der Datei in die
          Zwischenablage. </p></li><li><p>&Ouml;ffnen Sie im Intranator das Men&uuml; <span class="guimenu">System&nbsp;&gt;
            Schl&uuml;ssel&nbsp;&gt; Fremde Schl&uuml;ssel</span> und legen einen neuen Schl&uuml;ssel
          an. Vergeben Sie einen Namen f&uuml;r den Schl&uuml;ssel (z.B. den Namen des Mitarbeiters) und f&uuml;gen
          dann die Zertifikatsdaten aus der Zwischenablage in das Feld <span class="guilabel">Copy &amp;
            Paste Schl&uuml;ssel</span> ein.</p></li></ol></div></div></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="vpn-macosx-intranator.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="vpn.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="vpn-iphone-certificate-intranator.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">44.5. Intranator&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;45.2. Zertifikat f&uuml;r den Intranator</td></tr></table></div>
      
