    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">29.3. Besonderheiten und Tipps</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="webgroupware-activesync-serverconfig.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">29. Kapitel - Mobile Ger&auml;te per ActiveSync anbinden</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="webgroupware-activesync-android.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="webgroupware-activesync-tips"></a>29.3. Besonderheiten und Tipps</h2></div></div></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="webgroupware-activesync-tips-imapdelete"></a>29.3.1. L&ouml;schen von E-Mails auf dem Server</h3></div></div></div><p>Wird ein E-Mail-Konto parallel zum ActiveSync-Mobilger&auml;t auch mit einem
                E-Mail-Client per IMAP verwendet, der das L&ouml;schen von E-Mails nicht &uuml;ber das
                Verschieben in einen Papierkorb, sondern &uuml;ber L&ouml;schmarkierungen abbildet, werden die
                von diesem E-Mail-Client gel&ouml;schten E-Mails erst zum Zeitpunkt des endg&uuml;ltigen
                L&ouml;schens (Expunge-Befehl) auf dem ActiveSync-Ger&auml;t gel&ouml;scht.</p><p>Betroffene E-Mail-Clients sind u.a. Mozilla Thunderbird und Microsoft Outlook
                2003.</p></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="webgroupware-activesync-tips-syncsteps"></a>29.3.2. Synchronisationsschritte</h3></div></div></div><p>Meldet sich das Mobilger&auml;t am Intranator Server zur Synchronisation, bekommt es
                alle &Auml;nderungen seit der letzten Synchronisation mitgeteilt und aktualisiert
                daraufhin seine Daten. Genauso werden auf dem Mobilger&auml;t vorgenommene &Auml;nderungen an
                den Intranator Server &uuml;bertragen.</p><p>Aus technischen Gr&uuml;nden ben&ouml;tigen einige &Auml;nderungsaktionen allerdings zwei
                Synchronisationsschritte zur vollst&auml;ndigen &Uuml;bertragung. Zwischen diesen Schritten
                m&uuml;ssen mindestens 4 Minuten Zeitabstand sein. Beachten Sie dies vor allem bei
                mehrst&uuml;ndigen Synchronisationsintervallen. Rufen Sie im Zweifelsfalle die manuelle
                Synchronisation zwei mal hintereinander im Abstand von 4 Minuten auf dem Mobilger&auml;t
                auf. Sp&auml;testens dann sind alle Daten aktuell.</p></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="webgroupware-activesync-tips-manage-reset"></a>29.3.3. Ger&auml;te verwalten und neu synchronisieren</h3></div></div></div><p>Auf dem Intranator Server wird im Men&uuml;
                    <span class="guimenu">Benutzermanager&nbsp;&gt;&nbsp;Benutzer&nbsp;:&nbsp;Groupware</span>
                eine Liste aller mit dem entsprechenden Benutzerkonto verbundenen Ger&auml;te angezeigt.
                Diese ist gleichzeitig auch f&uuml;r jeden Benutzer selbst unter
                    <span class="guimenu">Benutzermanager&nbsp;&gt;&nbsp;Eigenes
                    Profil&nbsp;&gt;&nbsp;Groupware</span> erreichbar.</p><p>&Uuml;ber die Schaltfl&auml;che <span class="guibutton">Zur&uuml;cksetzen</span> wird der
                Synchronisationsstand eines Ger&auml;ts auf dem Server verworfen. Meldet sich das Ger&auml;t
                das n&auml;chste Mal zur Synchronisation, werden alle Daten erneut &uuml;bertragen. Auf diese
                Weise k&ouml;nnen Synchronisations- oder Datenkonsistenzprobleme gel&ouml;st werden. Dies wird
                auch automatisch beim R&uuml;ckspielen eines Backups des Intranator Servers
                ausgel&ouml;st.</p></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="webgroupware-activesync-tips-multiaccount"></a>29.3.4. Synchronisieren von mehreren Kalendern oder Kontakteordnern</h3></div></div></div><p>&Uuml;ber eine ActiveSync-Verbindung wird f&uuml;r jeden Groupware-Objekttyp (Termin,
                Adresse, Aufgabe,...) immer nur ein Ordner &uuml;bertragen. In einigen F&auml;llen ist aber
                gew&uuml;nscht z.B. zus&auml;tzlich zum privaten Adressbuch noch ein firmenweites Adressbuch
                zu &uuml;bertragen.</p><p> Dies kann durch das Einrichten zus&auml;tzlicher ActiveSync-Verbindungen realisiert
                werden. Da die zu &uuml;bertragenden Ordner pro Benutzerkonto im Men&uuml;
                    <span class="guimenu">Benutzermanager&nbsp;&gt;&nbsp;Benutzer&nbsp;:&nbsp;Groupware</span>
                eingestellt werden, muss f&uuml;r jede dieser Verbindungen ein unterschiedliches
                Benutzerkonto verwendet werden.</p><p>Soll z.B. ein firmenweites Adressbuch &uuml;bertragen werden, kann daf&uuml;r direkt ein
                allgemeiner Benutzer wie z.B. <strong class="userinput"><code>info</code></strong> unter dem auch das
                gew&uuml;nschte Adressbuch abgelegt ist, genutzt werden. Es gibt keine Beschr&auml;nkung wie
                viele ActiveSync-Verbindungen parallel mit einem Benutzerkonto verbunden sein
                k&ouml;nnen.</p><p>Werden auf diese Weise Kalender oder Aufgabenlisten eingebunden, gelten
                Erinnerungseinstellungen darin immer f&uuml;r das gesamte Benutzerkonto. Wird dies von
                mehreren Benutzern geteilt, erscheinen die Erinnerungen auch bei allen.</p></div></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="webgroupware-activesync-serverconfig.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="webgroupware-activesync.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="webgroupware-activesync-android.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">29.2. Einstellungen auf dem Server&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;30. Kapitel - ActiveSync mit Android-Ger&auml;ten</td></tr></table></div>
      
