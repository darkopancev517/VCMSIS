                        Seite, Men&uuml;punkt <span class="guimenuitem">Speichern</span>. Sie k&ouml;nnen nun in
                        Ihrem Browser das passende Zielverzeichnis w&auml;hlen.</p><div class="screenshot"><div class="mediaobject"><img src="../images/webgroupware/email-export02.png"></div></div></li><li><p>M&ouml;chten Sie die exportierte E-Mail z.B. zur Fehleranalyse wieder per E-Mail weiterleiten, so
                        packen Sie die .EML-Datei vorher am besten nochmal mit einem Packprogramm wie z.B. Zip. So wird
