    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">11.10. Masquerading / NAT</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="base-internet-fallback.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">11. Kapitel - Internet</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="base-internet-wakeup-call.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="base-internet-masquerading"></a>11.10. Masquerading / NAT</h2></div></div></div><a class="indexterm" name="base-internet-masquerading1"></a><p>Alle lokalen IP Adressen werden beim Internetzugriff maskiert und auf die externe IP des
      Intranators umgelegt (n:1 NAT / Masquerading). Nur f&uuml;r IPs aus Netzen mit dem Modus "LAN ohne
      NAT" wird kein NAT durchgef&uuml;hrt (siehe <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="base-intranet.php#base-intranet-interfaces">Abschnitt&nbsp;9.1, &#8222;IPs und Netze&#8220;</a>).</p><p>Das Masquerading kann einige Protokolle durcheinanderbringen. Die wichtigsten werden vom
      Intranator vollautomatisch korrigiert:</p><p>Aktives FTP, PPTP, IRC, Quake, Cuseeme, Realaudio, Vdolive.</p><p>F&uuml;r die fehlenden (z.B. ICQ oder Gnutella) verf&uuml;gt der Intranator &uuml;ber einen Socks 5
      Proxyserver auf Port 1080. Alle Rechner mit Vollzugriff k&ouml;nnen diesen ohne extra Login
      verwenden. Er muss nur unter
      Dienste&nbsp;&gt;&nbsp;Proxy&nbsp;&gt;&nbsp;Socks aktiviert
      werden.</p><p>Bei einigen Protokollen ist es zus&auml;tzlich n&ouml;tig, die Option &#8222;Eingehende Socks Verbindungen
      aktiviert&#8220; in der Firewallkonfiguration f&uuml;r den entsprechenden Provider zu verwenden.</p><a class="indexterm" name="d0e2569"></a></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="base-internet-fallback.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="base-internet.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="base-internet-wakeup-call.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">11.9. Ausweichen auf andere Provider im Fehlerfall (Fallback)&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;11.11. Lockruf</td></tr></table></div>
      
