    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">15.8. Archivierung</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="base-email-filter.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">15. Kapitel - E-Mail</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="base-email-automatic.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="base-email-archiving"></a>15.8. Archivierung</h2></div></div></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="base-email-archiving-interface"></a>15.8.1. Schnittstelle</h3></div></div></div><a class="indexterm" name="base-email-archiving-interface1"></a><p>Im Men&uuml; Dienste&nbsp;&gt;&nbsp;E-Mail&nbsp;&gt;&nbsp;Archivierung kann die
        Archivierungsschnittstelle des Intranators konfiguriert werden. Die Schnittstelle kann die
        E-Mails in verschiedenen Formaten schreiben und so an die eingesetzte Archivsoftware
        angepasst werden. Die verschiedenen Archivierungsmodi sind im Einzelnen:</p><div class="informaltable"><table border="1"><colgroup><col><col></colgroup><tbody><tr><td align="left">E-Mail-Kopie an</td><td align="left">Von jeder E-Mail wird eine Kopie an diese Adresse gesendet. Die urspr&uuml;nglichen
                Empf&auml;nger der E-Mail werden in der Kopfzeile
                  <code class="computeroutput">X-Envelope-To</code> abgelegt.</td></tr><tr><td align="left">Einzelne Dateien (BSMTP-Format)</td><td align="left">Jede E-Mail wird in eine einzelne Datei im BSMTP-Format geschrieben. Das
                BSMTP-Format ist in <a class="ulink" href="http://tools.ietf.org/html/rfc2442" target="_top">RFC 2442</a>
                definiert. In jeder Datei wird nur eine E-Mail abgelegt, mehrere Empf&auml;nger werden in
                einzelnen <code class="computeroutput">RCPT-TO</code>-Zeilen angegeben.</td></tr><tr><td align="left">Einzelne Dateien (EML/RFC822-Format)</td><td align="left">Der Inhalt (Header und Body) jeder E-Mail wird in eine einzelne Datei
                geschrieben. Dies wird normalerweise EML-Format genannt und wurde erstmals in <a class="ulink" href="http://tools.ietf.org/html/rfc822" target="_top">RFC 822</a> beschrieben. In jeder
                Datei wird nur eine E-Mail abgelegt, pro Empf&auml;nger wird eine separate Datei
                angelegt. Der Absender wird in der Kopfzeile
                  <code class="computeroutput">X-Envelope-From</code>, der Empf&auml;nger in der Kopfzeile
                  <code class="computeroutput">X-Envelope-To</code> abgelegt. </td></tr><tr><td align="left">MailStore Proxy</td><td align="left">Die einzelnen E-Mails werden in Dateien kompatibel zum Format des MailStore
                Proxys abgelegt. Damit kann der Intranator wie ein MailStore Proxy an den MailStore
                Server angebunden werden. Hinweise zur Einrichtung finden Sie in <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="base-email-archiving.php#base-email-archiving-mailstore">Abschnitt&nbsp;15.8.2, &#8222;Anbindung des MailStore Servers&#8220;</a>.
              </td></tr></tbody></table></div><p>Ist der Spamfilter des Intranators aktiv, k&ouml;nnen als Spam erkannte E-Mails von der
        Archivierung ausgeschlossen werden. W&auml;hlen Sie einen Schwellwert ab dem E-Mails nicht
        archiviert werden sollen. Wir empfehlen hier den Wert 8 zu verwenden. Weitere Details zu den
        Spam-Schwellwerten finden Sie in <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="base-email-filter.php#base-email-filter-spam-marking">Abschnitt&nbsp;15.7.1.2, &#8222;Markierung&#8220;</a></p><p>Haben Sie einen Archivierungsmodus gew&auml;hlt, der Dateien ablegt, kann &uuml;ber eine
        Windows-Freigabe auf diese zugegriffen werden. Sie m&uuml;ssen ein Login und Passwort f&uuml;r diese
        Freigabe w&auml;hlen. Die Archivierungsschnittstelle stellt nur vollst&auml;ndige Dateien zur
        Verf&uuml;gung. Die Schnittstelle stellt sicher, dass keine unvollst&auml;ndigen oder nur teilweise
        geschriebenen Dateien sichtbar werden oder abgerufen werden k&ouml;nnen.</p><div class="caution" style="margin-left: 0.2in; margin-right: 0.5in;"><table border="0" summary="Caution"><tr><td rowspan="2" align="center" valign="top" width="25"><img alt="[Achtung]" src="../images/admon/caution.png"></td><th align="left">Achtung</th></tr><tr><td align="left" valign="top"><p>Die Archivierungssoftware ist daf&uuml;r verantwortlich, dass die E-Mails unverz&uuml;glich nach
          der Archivierung von der Schnittstelle gel&ouml;scht werden. Die Schnittstellen-Freigabe ist
          nicht f&uuml;r ein dauerhaftes Speichern der E-Mail-Dateien ausgelegt und kann den Mailtransfer
          blockieren, wenn die Dateien nicht regelm&auml;&szlig;ig abgerufen werden.</p></td></tr></table></div><a class="indexterm" name="d0e4523"></a></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="base-email-archiving-mailstore"></a>15.8.2. Anbindung des MailStore Servers</h3></div></div></div><a class="indexterm" name="base-email-archiving-mailstore1"></a><a class="indexterm" name="base-email-archiving-mailstore2"></a><p>Der <a class="ulink" href="http://www.mailstore.com/" target="_top">MailStore Server</a> wird &uuml;ber die im
        Intranator vorhandene MailStore Proxy Schnittstelle angebunden. Dabei wird von jeder E-Mail,
        die durch den Intranator geleitet wird, eine Kopie angelegt und in einem speziellen Format
        an der Archivschnittstelle des Intranators abgelegt. Der MailStore Server ruft jetzt
        regelm&auml;&szlig;ig die Dateien an dieser Schnittstelle ab und f&uuml;gt sie dem Archiv hinzu.</p><p>Im Gegensatz zu den anderen Archivierungsmethoden des MailStore Servers (wie z.B.
        IMAP-Postfach oder Exchange Server) ist dadurch sichergestellt, dass wirklich alle E-Mails
        archiviert werden. Es ist nicht m&ouml;glich, dass der Benutzer eine ungl&uuml;cklich konfigurierte
        Sortierregel oder ein Programmfehler E-Mails l&ouml;scht, bevor sie archiviert wurden. </p><p>Gehen Sie bei der Installation wie folgt vor:</p><div class="orderedlist"><ol type="1"><li><p>Installieren Sie den MailStore Server wie im Handbuch des Herstellers beschrieben:
              <a class="ulink" href="http://de.help.mailstore.com/" target="_top">http://de.help.mailstore.com/</a>.</p></li><li><p>Stellen Sie den Archivierungsmodus des Intranators im Men&uuml;
            Dienste&nbsp;&gt;&nbsp;E-Mail&nbsp;&gt;&nbsp;Archivierung auf <span class="guilabel">MailStore
              Proxy</span> und geben Zugangsdaten f&uuml;r den Freigabepfad an.</p></li><li><p>Starten Sie auf dem Rechner mit dem MailStore Server den Windows Explorer (nicht
            verwechseln mit dem Internet Explorer) und &ouml;ffnen den in der Oberfl&auml;che des Intranators
            angezeigten Freigabepfad. Verkn&uuml;pfen Sie die <code class="filename">archive</code>-Freigabe
            dauerhaft mit einem Laufwerksbuchstaben und lassen das Passwort von Windows
            speichern.</p></li><li><p>&Ouml;ffnen Sie den <span class="command"><strong>MailStore Client</strong></span>, loggen sich mit
            Administrationsrechten ein und &ouml;ffnen das Men&uuml; <span class="guimenu">Verwaltung</span>.</p></li><li><p>Machen Sie &uuml;ber die Schaltfl&auml;che <span class="guibutton">Neuer Benutzer</span> jeden
            Benutzer Ihres Systems auch dem MailStore Server bekannt. Dabei ist vor allem wichtig,
            dass im Feld <span class="guilabel">E-Mail-Adressen</span> alle E-Mail-Adressen inkl. Aliases und
            Weiterleitungen des Benutzers eingetragen sind.</p><div class="mediaobject"><img src="../images/base/mailstore-user.png"></div></li><li><p>&Ouml;ffnen Sie das Men&uuml; <span class="guimenu">E-Mails archivieren</span> und konfigurieren ein
            neues Archivierungsprofil vom Typ <span class="guilabel">MailStore Proxy</span>.</p></li><li><p>W&auml;hlen Sie den eben verkn&uuml;pften Laufwerksbuchstaben als Verzeichnis und lassen die
            Reportdateien l&ouml;schen, wenn die Archivierung erfolgreich war (wichtig!).</p><div class="mediaobject"><img src="../images/base/mailstore-profile-01.png"></div><div class="mediaobject"><img src="../images/base/mailstore-profile-02.png"></div></li><li><p>Legen Sie &uuml;ber die Schaltfl&auml;che <span class="guibutton">Zeitgesteuert</span> fest, dass das
            Archivierungsprotokoll jede Minute ausgef&uuml;hrt werden soll. </p><div class="mediaobject"><img src="../images/base/mailstore-profile-time.png"></div></li></ol></div><a class="indexterm" name="d0e4612"></a><a class="indexterm" name="d0e4613"></a></div></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="base-email-filter.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="base-email.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="base-email-automatic.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">15.7. E-Mail-Filter&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;15.9. Automatischer Transfer</td></tr></table></div>
      
