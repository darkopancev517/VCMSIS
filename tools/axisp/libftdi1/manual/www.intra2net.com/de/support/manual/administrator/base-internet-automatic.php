    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">11.8. Verbindungsautomatik</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="base-internet-official.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">11. Kapitel - Internet</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="base-internet-fallback.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="base-internet-automatic"></a>11.8. Verbindungsautomatik</h2></div></div></div><a class="indexterm" name="base-internet-automatic1"></a><p>Unter Netzwerk&nbsp;&gt;&nbsp;Provider&nbsp;&gt;&nbsp;Automatik
      wird festgelegt, welcher Provider normalerweise f&uuml;r den Verbindungsaufbau verwendet wird.
      Au&szlig;erdem wird der Modus des Verbindungsaufbaus konfiguriert.</p><p>Bei manuellem Verbindungsaufbau geht der Intranator nur Online, wenn ein Benutzer auf der
      Hauptseite auf <span class="guibutton">Online</span> klickt oder eine zeitgesteuerte Aktion (z.B.
      automatischer E-Mail-Transfer) gestartet wird.</p><p>Bei Verbindungsaufbau bei Bedarf geht der Intranator erst online, sobald ein Rechner mit
      entsprechenden Firewall-Zugriffsrechten ein Paket ins Internet senden will. Auch
      zeitgesteuerte Aktionen (wie z.B. automatischer E-Mail-Transfer) l&ouml;sen einen W&auml;hlvorgang
      aus.</p><p>Ist der Intranator auf immer online gestellt, versucht der Intranator st&auml;ndig eine
      Verbindung offen zu halten.</p><p>Bei manuellem und bei Verbindungsaufbau bei Bedarf bleibt der Intranator solange online,
      bis der Provider die Verbindung beendet (z.B. durch Zwangstrennung) oder (sofern konfiguriert)
      die Verbindung f&uuml;r eine gewisse Zeit nicht genutzt wird (Verbindungs-Timeout).</p><a class="indexterm" name="base-internet-automatic2"></a><p>Es kann eine Uhrzeit festgelegt werden, zu der der Intranator auf jeden Fall die
      Verbindung kurz trennt. Dies kann sinnvoll sein um z.B. eine Zwangstrennung durch den Provider
      nach 24 Stunden auf eine feste Zeit in der Nacht zu legen. Nach der Zwangstrennung wird die
      Verbindung nur im Modus <span class="guimenuitem">immer online</span> sofort wieder
      aufgebaut.</p><a class="indexterm" name="d0e2513"></a><a class="indexterm" name="d0e2514"></a></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="base-internet-official.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="base-internet.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="base-internet-fallback.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">11.7. Offizielle IPs und DMZ&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;11.9. Ausweichen auf andere Provider im Fehlerfall (Fallback)</td></tr></table></div>
      
