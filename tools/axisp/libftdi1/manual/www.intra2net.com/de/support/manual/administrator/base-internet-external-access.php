      die E-Mails vom Internet aus. Au&szlig;erdem kann man vom Internet aus per HTTPS auf die Oberfl&auml;che
      und Web-Groupware zugreifen. </p><p>Dies wird &uuml;ber die unter
      Netzwerk&nbsp;&gt;&nbsp;Provider&nbsp;&gt;&nbsp;Profile&nbsp;:&nbsp;Firewall
      eingestellte Firewallregelliste konfiguriert.</p><p>F&uuml;r HTTPS Verbindungen ist es m&ouml;glich, einzustellen, ob nur auf das Webmail-System oder auf
      die komplette Oberfl&auml;che zugegriffen werden soll. Das h&auml;ngt von den Benutzergruppen ab, in
      denen der angemeldete Benutzer ist. Die Rechte f&uuml;r den Zugriff von au&szlig;en werden unter
      Benutzermanager&nbsp;&gt;&nbsp;Gruppen&nbsp;&gt;&nbsp;Administrationsrechte
      eingestellt.</p><p>F&uuml;r die Verbindungen von au&szlig;en ist es sinnvoll, einen anderen SSL-Schl&uuml;ssel als f&uuml;r die
      Verbindungen von innen zu verwenden, denn die Browser vergleichen den DNS Namen einer Webseite
      und den Namen im Schl&uuml;ssel um sicherzustellen, dass kein Man-in-the-Middle Angriff ausgef&uuml;hrt
      wird. </p><p>Legen Sie also unter System&nbsp;&gt;&nbsp;Schl&uuml;ssel einen X.509 Schl&uuml;ssel mit
      dem DynDNS Namen des Intranators als Rechnername (CN) an (siehe auch <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="base-ssl-encryption-config.php">Abschnitt&nbsp;10.2, &#8222;Zertifikate richtig erstellen&#8220;</a>).</p><a class="indexterm" name="d0e2786"></a><a class="indexterm" name="d0e2787"></a><a class="indexterm" name="d0e2788"></a><a class="indexterm" name="d0e2789"></a></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="base-internet-dyndns.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="base-internet.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="base-proxy.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">11.12. DynDNS&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;12. Kapitel - Proxy</td></tr></table></div>
      
