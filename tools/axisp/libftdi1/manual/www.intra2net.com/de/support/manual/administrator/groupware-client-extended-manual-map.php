    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">22.2. Ordner manuell verbinden</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="groupware-client-extended.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">22. Kapitel - Erweiterte Funktionen</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="groupware-client-extended-inbox.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="groupware-client-extended-manual-map"></a>22.2. Ordner manuell verbinden</h2></div></div></div><p>Normalerweise verbindet der Intra2net Groupware Client die Ordner automatisch zwischen
            dem Server und Outlook:</p><div class="itemizedlist"><ul type="disc"><li><p>Auf dem Server angelegte Groupware-Ordner (nicht E-Mail-Ordner) erscheinen
                    automatisch auch in Outlook</p></li><li><p>In Outlook angelegte Ordner werden automatisch auf dem Server angelegt und mit
                    diesem verbunden</p></li><li><p>Lokal gel&ouml;schte Ordner werden auch auf dem Server gel&ouml;scht</p></li><li><p>Auf dem Server gel&ouml;schte Ordner werden lokal in den Ordner
                        <span class="guilabel">Gesicherte Daten</span> verschoben</p></li><li><p>Ordnernamen und Hierarchie sind in Outlook und auf dem Server identisch</p></li></ul></div><p>Der Benutzer muss die Ordner nicht einzeln manuell verbinden, bekommt daf&uuml;r aber keine
            M&ouml;glichkeit lokal in Outlook die Ordnerhierarchie umzugestalten oder Ordner lokal anders
            zu benennen als auf dem Server.</p><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="groupware-client-extended-manual-map-switch"></a>22.2.1. Umstellen auf Manuelles Verbinden</h3></div></div></div><p>Um dies zu erm&ouml;glichen gibt es auch die M&ouml;glichkeit die Automatik abzuschalten und
                die Ordner manuell zu verbinden. Gehen Sie daf&uuml;r wie folgt vor:</p><div class="procedure"><ol type="1"><li><p>&Ouml;ffnen Sie das Men&uuml; <span class="guimenu">Groupware Client&nbsp;&gt;&nbsp;Ordner
                            verbinden</span>.</p></li><li><p>Klicken Sie den Wurzelordner auf der linken Seite (Outlook) an und
                        entfernen die Checkbox vor dem Namen.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/extended-switch-to-manual.png"></div></div></li><li><p>Sie werden gefragt ob Sie die Verbindung wirklich aufheben wollen.
                        Antworten Sie mit <span class="guibutton">OK</span>.</p></li></ol></div><p>Die einzelnen Unterordner sind zuerst weiterhin verbunden wie bisher. Wenn lokal
                oder auf dem Server neue Ordner auf der obersten Ordnerebene anlegt werden, m&uuml;ssen
                diese aber ab sofort manuell verbunden werden (wenn gew&uuml;nscht). Au&szlig;erdem ist es
                jetzt m&ouml;glich die Verbindung einzelner Ordner aufzuheben oder diese an einer anderen
                Stelle in der Ordnerhierarchie als auf dem Server zu verbinden.</p></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="groupware-client-extended-manual-map-folder"></a>22.2.2. Einen einzelnen Ordner verbinden</h3></div></div></div><div class="procedure"><ol type="1"><li><p>&Ouml;ffnen Sie das Men&uuml; <span class="guimenu">Groupware Client&nbsp;&gt;&nbsp;Ordner
                            verbinden</span>.</p></li><li><p>Auf der rechten Seite wird das Konto auf dem Server angezeigt, auf der
                        linken die Ordnerhierarchie im lokalen Outlook.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/config-sync-single01.png"></div></div></li><li><p>Ziehen Sie den gew&uuml;nschten Ordner auf der Outlook-Seite, hier Aufgaben,
                        mit gedr&uuml;ckter Maustaste auf den Stammordner auf der rechten Seite und
                        lassen dort die Maustaste los (Drag &amp; Drop). Befindet sich der zu
                        verbindende Ordner auf dem Server, so ziehen Sie ihn in die andere
                        Richtung.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/config-sync-single02.png"></div></div><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/config-sync-single03.png"></div></div></li></ol></div><p>Sie haben nun die Wahl wie die Verbindung genau gestaltet werden soll:</p><div class="informaltable"><table border="1"><colgroup><col align="left"><col align="left"></colgroup><tbody><tr><td align="left">Verkn&uuml;pfungstyp</td><td align="left">
                                <div class="itemizedlist"><ul type="disc"><li><p>Einfach: Es wird nur der eine ausgew&auml;hlte Ordner
                                            verbunden. Unterordner des Ordners werden nicht
                                            verbunden.</p></li><li><p>Baum: Der Ordner wird inkl. aller seiner Unterordner
                                            verbunden. Werden lokal in Outlook oder auf dem Server
                                            neue Ordner hinzugef&uuml;gt oder gel&ouml;scht, wird die
                                            Verbindung so angepasst, dass die Ordnerhierarchie lokal
                                            in Outlook und auf dem Server synchron sind.</p></li></ul></div>
                            </td></tr><tr><td align="left">Neuer Unterordner</td><td align="left">Wenn aktiviert, wird unterhalb des oben angegebenen Ziel-Ordners
                                ein neuer Ordner angelegt und dieser mit dem Server
                                verbunden.</td></tr><tr><td align="left">Ordnername</td><td align="left">Soll ein neuer Ordner angelegt werden, kann hier der Name
                                eingestellt werden. Damit k&ouml;nnen lokal in Outlook und auf dem Server
                                unterschiedliche Namen f&uuml;r den selben Ordner verwendet werden. So
                                kann z.B. der Ordner <code class="computeroutput">Kalender</code> des
                                Benutzers <code class="computeroutput">meier</code> lokal in Outlook
                                    <strong class="userinput"><code>Kalender meier</code></strong> genannt werden.</td></tr></tbody></table></div><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/config-sync-single04.png"></div></div></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="groupware-client-extended-manual-map-remove"></a>22.2.3. Verbindung eines Ordners aufheben</h3></div></div></div><p>Um die Verbindung eines manuell verbundenen Ordners aufzuheben gehen Sie wie folgt
                vor:</p><div class="procedure"><ol type="1"><li><p>&Ouml;ffnen Sie das Men&uuml; <span class="guimenu">Groupware Client&nbsp;&gt;&nbsp;Ordner
                            verbinden</span>.</p></li><li><p>Markieren Sie den Ordner, dessen Verbindung Sie aufheben m&ouml;chten, auf der
                        linken Seite (Outlook).</p></li><li><p>Klicken Sie die Checkbox vor dem Ordnernamen an.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/extended-unmap-folder.png"></div></div></li><li><p>Best&auml;tigen Sie, dass Sie die Verbindung aufheben wollen.</p></li></ol></div><div class="note" style="margin-left: 0.2in; margin-right: 0.5in;"><table border="0" summary="Note"><tr><td rowspan="2" align="center" valign="top" width="25"><img alt="[Hinweis]" src="../images/admon/note.png"></td><th align="left">Hinweis</th></tr><tr><td align="left" valign="top"><p>Diese Vorgehensweise funktioniert nur f&uuml;r manuell verbundene Ordner. Handelt
                    es sich um den Unterordner einer Baum-Verbindung, gehen Sie stattdessen vor wie
                    in <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="groupware-client-extended.php#groupware-client-extended-skip">Abschnitt&nbsp;22.1, &#8222;Ordner von der Synchronisation ausschlie&szlig;en&#8220;</a> beschrieben.</p></td></tr></table></div></div></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="groupware-client-extended.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="groupware-client-extended.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="groupware-client-extended-inbox.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">22. Kapitel - Erweiterte Funktionen&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;22.3. Posteingang/Meldungen</td></tr></table></div>
      
