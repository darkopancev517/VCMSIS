            Datendatei in ein Outlook-Profil eingebunden wird. Legen Sie wie im Folgenden
            beschrieben ein neues, leeres Profil an, konfigurieren ein E-Mail-Konto und binden die
            spezielle Datendatei ein.</p><p>Legen Sie unbedingt immer ein neues Profil an, auch wenn Sie Daten aus einer
            bestehenden Outlook-Konfiguration &uuml;bernehmen wollen. Bestehende Daten k&ouml;nnen nach der
            Grundkonfiguration in das neue Profil importiert werden. Dies wird in <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="groupware-client-account-import.php">Abschnitt&nbsp;20.3, &#8222;Bestehende Daten &uuml;bernehmen&#8220;</a>
            beschrieben.</p><div class="procedure"><ol type="1"><li><p>Installieren Sie das Zertifikat des Intranator Servers auf diesem Client. Die n&ouml;tigen
                    Schritte finden Sie im <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="base-ssl-encryption.php">10. Kapitel,&nbsp;&#8222;SSL-Verschl&uuml;sselung und Zertifikate&#8220;</a> beschrieben. Fahren Sie erst hier fort,
                    wenn dies erfolgreich abgeschlossen ist.</p></li><li><p>&Ouml;ffnen Sie die Windows-Systemsteuerung, Men&uuml;punkt <span class="guimenuitem">Mail
                        (32-Bit)</span>.</p></li><li><p>&Ouml;ffnen Sie den Profil-Editor</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile01.png"></div></div></li><li><p>F&uuml;gen Sie ein neues Profil hinzu und vergeben einen Namen</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile02.png"></div></div><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile03.png"></div></div></li><li><a class="indexterm" name="groupware-client-installation-config-ol2007-3"></a><p>Konfigurieren Sie die Servereinstellungen f&uuml;r Internet-E-Mail.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile04.png"></div></div><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile05.png"></div></div></li><li><p>Stellen Sie den Kontotyp auf IMAP und tragen die Benutzer- und Serverdaten
                    ein.</p><p>Verwenden Sie als Postein- und -ausgangsserver Ihren Intranator Server.
                    Verwenden Sie unbedingt den vollst&auml;ndigen DNS-Namen inkl. Domain Ihres
                    Intranator Servers, tragen Sie keine IP-Adressen ein.</p><p>Soll der Client auch von au&szlig;erhalb des lokalen Netzes zugreifen k&ouml;nnen, so
                    verwenden Sie den externen DNS-Namen des Intranator Servers. Verwenden Sie auch
                    hier keine IP-Adresse, sondern registrieren gegebenenfalls f&uuml;r Ihren Intranator
                    Server einen DNS-Namen bei Ihrem Domainprovider oder DynDNS-Anbieter.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile-imap01.png"></div></div></li><li><p>&Ouml;ffnen Sie die <span class="guibutton">Weiteren Einstellungen</span> und den Reiter
                        <span class="guimenu">Postausgangsserver</span>. Aktivieren Sie die
                    SMTP-Authentifizierung mit den gleichen Einstellungen wie f&uuml;r den
                    Posteingangsserver.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile-imap02.png"></div></div></li><li><p>&Ouml;ffnen Sie den Reiter <span class="guimenu">Erweitert</span>. Stellen Sie die
                    Verschl&uuml;sselung f&uuml;r IMAP auf <span class="guimenuitem">SSL</span> und die f&uuml;r SMTP auf
                        <span class="guimenuitem">TLS</span>. &Auml;ndern Sie die Portnummer f&uuml;r den
                    Postausgangsserver auf <strong class="userinput"><code>587</code></strong> (f&uuml;r SMTP Submission / MSA).
                    Schlie&szlig;en Sie die E-Mail-Einstellungen.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile-imap03.png"></div></div></li><li><p>Testen Sie die Kontoeinstellungen.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile-imap04.png"></div></div></li><li><p>Der Test muss erfolgreich abgeschlossen werden.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile-imap06.png"></div></div><p>Wird Ihnen stattdessen eine Internetsicherheitswarnung angezeigt, so ist das
                    Zertifikat nicht korrekt konfiguriert oder installiert. Wiederholen Sie Schritt
                    1 und versuchen es erneut bis der Test erfolgreich ist.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile-imap05.png"></div></div></li><li><p>Beenden Sie die Kontokonfiguration und &ouml;ffnen Sie die Eigenschaften des neuen
                    Profils. W&auml;hlen Sie Bearbeiten der Datendateien.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile-pst02.png"></div></div><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile-pst03.png"></div></div></li><li><p>F&uuml;gen Sie eine neue Datendatei vom Typ <span class="guimenuitem">Intranator Groupware
                        Client Datendatei (.mxstore)</span> hinzu.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile-pst04.png"></div></div><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile-pst05.png"></div></div></li><li><p>W&auml;hlen Sie einen Ordner in dem die Datendatei des Intra2net Groupware Clients
                    gespeichert werden soll, beispielsweise <code class="filename">Eigene
                        Dokumente\Outlook-Dateien</code>.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile-pst06.png"></div></div></li><li><p>Schlie&szlig;en Sie den Dialog.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile-pst07.png"></div></div></li><li><p>Wenn Sie m&ouml;chten, k&ouml;nnen Sie Outlook automatisch beim Start das eben erstellte
                    Profil &ouml;ffnen lassen.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/install-ol2007-profile-pst08.png"></div></div></li><li><p>Starten Sie Outlook mit dem eben neu erstellten Profil. Es &ouml;ffnet sich
                    automatisch der Dialog <span class="guimenu">Server-Konten</span> des Intra2net Groupware
                    Clients.</p></li><li><p>Fahren Sie mit der Einrichtung im <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="groupware-client-account.php">20. Kapitel,&nbsp;&#8222;Konten konfigurieren&#8220;</a> fort. </p></li></ol></div><a class="indexterm" name="d0e6384"></a><a class="indexterm" name="d0e6385"></a><a class="indexterm" name="d0e6386"></a></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="groupware-client-installation-config-ol2010.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="groupware-client-installation.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="groupware-client-installation-config-ol2003.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">19.4. Grundkonfiguration mit Outlook 2010&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;19.6. Grundkonfiguration mit Outlook 2003</td></tr></table></div>
      
