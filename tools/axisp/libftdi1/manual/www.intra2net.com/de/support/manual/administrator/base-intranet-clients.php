    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">9.4. Clients eintragen</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="base-intranet-dns.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">9. Kapitel - Intranet</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="base-intranet-dhcp.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="base-intranet-clients"></a>9.4. Clients eintragen</h2></div></div></div><a class="indexterm" name="base-intranet-clients1"></a><p>Unter Netzwerk&nbsp;&gt;&nbsp;Intranet&nbsp;&gt;&nbsp;Rechner
      k&ouml;nnen einzelne Rechner eingetragen werden. Jedem eingetragenen Rechner k&ouml;nnen damit eigene
      Zugriffsrechte (siehe <a class="xref" href="base-intranet-rights.php" title="9.2. Zugriffsrechte eines Netzwerkobjekts">Abschnitt&nbsp;9.2, &#8222;Zugriffsrechte eines Netzwerkobjekts&#8220;</a>) zugewiesen werden.</p><p>Au&szlig;erdem ist der Rechner automatisch per DNS unter seinen Namen erreichbar (Prim&auml;rer Name
      sowie den unter "Alias" eingetragenen sekund&auml;ren Namen). Ein Eintrag f&uuml;r Reverse-DNS (welchen
      DNS-Namen hat die IP x?) wird auch automatisch angelegt.</p><a class="indexterm" name="d0e1615"></a><p>Ist eine MAC Adresse eingestellt, so werden DHCP Anfragen mit dieser MAC mit der
      eingestellten IP beantwortet (statisches DHCP). Ist eine IP Adresse im Feld eingegeben, so
      kann mit einem Klick auf <span class="guibutton">Erkennen</span> die dazugeh&ouml;rige MAC gesucht
      werden.</p><a class="indexterm" name="d0e1625"></a><p>&Uuml;ber <span class="guibutton">Wake-On-LAN</span> k&ouml;nnen Sie ein spezielles IP-Paket (&#8222;Magic
      Packet&#8220;) an die angegebene MAC-Adresse senden. Die meisten Rechner k&ouml;nnen dadurch &uuml;ber das
      Netz eingeschaltet werden. Es kann sein, dass Sie Einstellungen im BIOS des Rechners vornehmen
      m&uuml;ssen um diese Funktion zu aktivieren.</p><a class="indexterm" name="base-intranet-clients2"></a><p>Wurde der Rechner &uuml;ber dynamisches DHCP angelegt, so wird angezeigt, bis wann sein Lease
      g&uuml;ltig ist. Wird es bis dahin nicht erneuert, wird der gesamte Eintrag gel&ouml;scht.</p><p>Auch bei Rechnern, die &uuml;ber dynamisches DHCP angelegt wurden, ist es m&ouml;glich, andere Rechte
      oder Aliasnamen einzutragen. Da diese Einstellungen jedoch verloren gehen, wenn der Rechner
      l&auml;nger nicht aktiv ist (z.B. Wochenende, Urlaub), empfehlen wir, solche Rechner aus dem
      dynamischen DHCP Pool zu nehmen und eine andere IP au&szlig;erhalb eines Pools zuzuweisen. &Auml;ndern
      Sie dazu einfach die IP und klicken auf <span class="guibutton">Einstellungen speichern</span>.</p><a class="indexterm" name="d0e1643"></a><a class="indexterm" name="d0e1644"></a></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="base-intranet-dns.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="base-intranet.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="base-intranet-dhcp.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">9.3. Domain und DNS&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;9.5. DHCP</td></tr></table></div>
      
