      welches Protokollmeldungen von allen Applikationen, wie dem VPN-System, entgegennimmt und
      zwischenspeichert. Zum Anzeigen und Exportieren wird eine zus&auml;tzliche App ben&ouml;tigt. Da die
      Logdateien typischerweise recht umfangreich und breit sind, empfiehlt es sich, sie nicht auf
      dem Ger&auml;t selbst zu analysieren, sondern sie per E-Mail oder Datei an einen PC zu senden. Es
      gibt verschiedene Apps, die diese Funktion beherrschen. </p><div class="informaltable"><table border="0"><colgroup><col align="left"><col align="right"></colgroup><tbody><tr><td align="left">
