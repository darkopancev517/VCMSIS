                  Neil Boyd</a> empfehlen.</p>
            </td><td align="right">
              <div class="mediaobject"><img src="../images/vpn/android-galaxys4/sendlog-qrcode.png"></div>
            </td></tr></tbody></table></div><p>W&auml;hlen Sie in <span class="application">SendLog</span> das Format
        <span class="guimenuitem">time</span> aus und senden Sie das Log z.B. per E-Mail an einen
      PC.</p><p>Eintr&auml;ge des VPN-Systems sind nach Datum und Uhrzeit mit dem Programmnamen &#8222;racoon&#8220;
