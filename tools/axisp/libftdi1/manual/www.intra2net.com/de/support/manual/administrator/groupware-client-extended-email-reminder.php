    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">22.9. Erinnerungen und Nachverfolgen von E-Mails</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="groupware-client-extended-multiplesenders.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">22. Kapitel - Erweiterte Funktionen</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="groupware-client-extended-private.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="groupware-client-extended-email-reminder"></a>22.9. Erinnerungen und Nachverfolgen von E-Mails</h2></div></div></div><a class="indexterm" name="d0e8065"></a><a class="indexterm" name="d0e8068"></a><p>Outlook bietet bei Verwendung eines POP-Kontos oder im Zusammenhang mit dem Exchange
            Server die M&ouml;glichkeit, Erinnerungen f&uuml;r E-Mails zu definieren und eine Liste von sp&auml;ter
            zu Bearbeitenden E-Mails zu erstellen (Nachverfolgen-Funktion). Dies ist bei Verwendung
            von IMAP-Konten (wie sie f&uuml;r den Groupware Client empfohlen werden) nicht m&ouml;glich.
            Au&szlig;erdem k&ouml;nnen solche Erinnerungen und Nachverfolgen-Informationen nicht auf
            Mobilger&auml;te &uuml;bertragen werden.</p><p>Daher empfehlen wir, statt der Nachverfolgen-Funktion f&uuml;r die E-Mail zu verwenden,
            eine separate Aufgabe zu erstellen. Diese kann dann in Outlook, der Webgroupware und auf
            per ActiveSync angebundenen Ger&auml;ten verwendet werden. Bei Bedarf kann sie auch f&uuml;r
            andere Nutzer freigegeben und von diesen dann bearbeitet werden, z.B. im Falle von
            Vertretung.</p><p>Ab Outlook 2010 kann das Anlegen mit der <span class="guimenu">QuickSteps</span>-Funktion
            automatisiert werden. Gehen Sie zum einmaligen Einrichten wie folgt vor:</p><div class="procedure"><ol type="1"><li><p>Klicken Sie mit der rechten Maustaste auf einen beliebigen bereits bestehenden
                    QuickStep und w&auml;hlen die Funktion <span class="guimenuitem">Neuer
                    QuickStep</span>.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/email-task-quickstep01.png"></div></div></li><li><p>W&auml;hlen Sie die Aktion <span class="guimenuitem">Eine Aufgabe mit Nachrichtentext
                        erstellen</span>.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/email-task-quickstep02.png"></div></div></li><li><p>Speichern Sie den QuickStep &uuml;ber die Schaltfl&auml;che <span class="guibutton">Fertig
                        stellen</span>.</p></li></ol></div><p>Der QuickStep ist nun fertig eingerichtet und kann verwendet werden. &Ouml;ffnen Sie dazu
            die gew&uuml;nschte E-Mail und klicken auf den vorher angelegten QuickStep. Es wird
            automatisch eine passende Aufgabe erzeugt.</p><div class="screenshot"><div class="mediaobject"><img src="../images/groupware-client/email-task-quickstep04.png"></div></div></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="groupware-client-extended-multiplesenders.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="groupware-client-extended.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="groupware-client-extended-private.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">22.8. Mehrere Absenderadressen&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;22.10. Kennzeichnung als Privat</td></tr></table></div>
      
