    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">22.3. Posteingang/Meldungen</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="groupware-client-extended-manual-map.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">22. Kapitel - Erweiterte Funktionen</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="groupware-client-extended-folderoptions.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="groupware-client-extended-inbox"></a>22.3. Posteingang/Meldungen</h2></div></div></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="groupware-client-extended-inbox-name"></a>22.3.1. Ordnername</h3></div></div></div><p>Im <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="groupware-client-account.php">20. Kapitel,&nbsp;&#8222;Konten konfigurieren&#8220;</a>
                empfehlen wir, E-Mails und Groupware-Objekte auf zwei verschiedene Datendateien
                aufzuteilen. Jede Datendatei hat normalerweise einen Ordner Namens
                    <code class="computeroutput">Posteingang</code>. Das zusammen h&auml;tte zur Folge, dass
                der Benutzer zwei Posteing&auml;nge angezeigt bekommt. Davon wird aber nur einer f&uuml;r neue
                E-Mails genutzt, der andere f&uuml;r Meldungen des Groupware Clients.</p><p>Letzterer wird daher mit dem Namen <code class="computeroutput">Meldungen</code>
                angezeigt.</p><p>Sollte entgegen unserer Empfehlung der Groupware Client auch f&uuml;r E-Mails und nicht
                nur f&uuml;r Groupware-Objekte genutzt werden, kann der
                    <code class="computeroutput">Meldungen</code>-Ordner mit dem Posteingang auf dem
                Server verbunden werden. Er bekommt dann automatisch wieder seinen urspr&uuml;nglichen
                Namen <code class="computeroutput">Posteingang</code>.</p><p>Bei Bedarf (z.B. f&uuml;r Kompatibilit&auml;t mit anderen Programmen und Add-Ins) kann
                dieses Verhalten auch unterdr&uuml;ckt werden, siehe hierzu <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="groupware-client-reference-registry.php#groupware-client-reference-registry-store">Abschnitt&nbsp;25.2.1, &#8222;Einstellungen f&uuml;r den Store&#8220;</a>.</p></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="groupware-client-extended-hierarchy"></a>22.3.2. Ordnerhierarchie</h3></div></div></div><p>In Outlook wird der Ordner <code class="computeroutput">Posteingang</code>
                normalerweise auf der selben Hierarchieebene wie
                    <code class="computeroutput">Kalender</code>,
                    <code class="computeroutput">Kontakte</code> etc. angezeigt. Auf dem IMAP-Server
                ist der <code class="computeroutput">Posteingang</code> dagegen der Wurzelordner eines
                Benutzers und alle anderen Ordner wie <code class="computeroutput">Kalender</code>,
                    <code class="computeroutput">Kontakte</code> etc. sind Unterordner des
                Posteingangs.</p><p>Der Groupware Client &uuml;bersetzt diese beiden unterschiedlichen Konzepte und stellt
                die Ordner innerhalb von Outlook so dar, wie es in Outlook &uuml;blich ist.</p><p>In Outlook ist es allerdings m&ouml;glich Unterordner des Posteingangs anzulegen. Auf
                dem IMAP-Server l&auml;sst sich ein solcher Unterschied zwischen Unterordnern des
                Posteingangs und Ordnern auf der selben Ebene des Posteingangs normalerweise nicht
                darstellen. Der Groupware Client legt in diesem Fall einen Ordner Namens
                    <code class="computeroutput">ibx_sub</code> auf dem IMAP-Server an und legt alle
                Unterordner des Outlook-Posteingangs darunter ab.</p></div></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="groupware-client-extended-manual-map.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="groupware-client-extended.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="groupware-client-extended-folderoptions.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">22.2. Ordner manuell verbinden&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;22.4. Ordneroptionen</td></tr></table></div>
      
