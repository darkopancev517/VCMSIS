          Intranator ein.</p></li><li><p>W&auml;hlen Sie &#8220;Secure Gateway Tunnel&#8221; mit ID Type &#8220;Any&#8221;. Tragen Sie entweder bei &#8220;Gateway
          Hostname&#8221; den DNS-Namen des Intranators oder bei &#8220;Gateway IP&#8221; die offizielle IP des
          Intranators ein.</p></li><li><p>Gehen Sie auf &#8220;My Identity&#8221; und w&auml;hlen das vorher mit makecert erstellte Zertifikat
          aus</p></li><li><p>Stellen Sie den Virtual Adapter auf &#8220;Required&#8221; und tragen die im Intranator
          hinterlegte virtuelle IP ein.</p><div class="mediaobject"><img src="../images/vpn/netgear-client/15-otherip.png"></div></li><li><p>Gehen Sie auf &#8220;Security Policy&#8221; und stellen den Negotiation Mode auf &#8220;Main Mode&#8221;.
          Stellen Sie PFS genauso wie auf dem Intranator ein (Diffie-Hellman Group 2 wenn
          aktiviert).</p><div class="mediaobject"><img src="../images/vpn/netgear-client/10-policy.png"></div></li><li><p>Gehen Sie auf das Proposal von Phase 1. W&auml;hlen Sie AES oder Triple-DES als Algorithmus
          und SHA-1 f&uuml;r den Hash.</p><div class="mediaobject"><img src="../images/vpn/netgear-client/11-auth.png"></div></li><li><p>Gehen Sie auf das Proposal von Phase 2. W&auml;hlen Sie hierf&uuml;r dieselben Algorithmen wie
          f&uuml;r Phase 1 und stellen die Encapsulation auf &#8220;Tunnel&#8221;.</p><div class="mediaobject"><img src="../images/vpn/netgear-client/12-keyex.png"></div></li><li><p>Speichern Sie die Konfiguration. Ist die Verbindung auch auf dem Intranator
          konfiguriert, kann sie jetzt aus dem Kontextmen&uuml; gestartet werden.</p><div class="mediaobject"><img src="../images/vpn/netgear-client/13-connect.png"></div></li></ol></div><a class="indexterm" name="d0e13038"></a><a class="indexterm" name="d0e13039"></a><a class="indexterm" name="d0e13040"></a></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="vpn-netgear-client-certificates.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="vpn-netgear-client.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="vpn-netgear-client-intranator.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">43.3. Zertifikate&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;43.5. Intranator</td></tr></table></div>
      
