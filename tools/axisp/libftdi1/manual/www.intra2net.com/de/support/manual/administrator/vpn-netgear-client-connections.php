          SafeNet Symbols in der Traybar.</p></li><li><p>Schalten Sie als erstes die Verwendung virtueller IPs frei. Gehen Sie dazu in das Men&uuml;
          &#8220;Options &gt; Global Policy Settings&#8221; und aktivieren Sie &#8220;Allow to Specify Internal
          Network Address&#8221;. Es macht Sinn auch gleich &#8220;Enable IPSec Logging&#8221; zu aktivieren um im
          Fehlerfall aussagef&auml;hige Protokolldateien zur Verf&uuml;gung zu haben.</p><div class="mediaobject"><img src="../images/vpn/netgear-client/14-global.png"></div></li><li><p>Klicken Sie auf das Symbol ganz links um eine neue Verbindung anzulegen. W&auml;hlen Sie
