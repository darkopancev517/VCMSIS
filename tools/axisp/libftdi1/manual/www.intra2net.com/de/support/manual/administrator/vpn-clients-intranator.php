    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">40.2. Konfiguration auf dem Intranator</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="vpn-clients.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">40. Kapitel - Anbinden von einzelnen PCs</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="vpn-ncp-secure-entry.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="vpn-clients-intranator"></a>40.2. Konfiguration auf dem Intranator</h2></div></div></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="vpn-clients-intranator-prereq"></a>40.2.1. Voraussetzungen</h3></div></div></div><p>Als erstes m&uuml;ssen Sie daf&uuml;r sorgen, dass jede Seite &uuml;ber einen eigenen Schl&uuml;ssel verf&uuml;gt
        und die Gegenseite den &ouml;ffentlichen Schl&uuml;ssel oder das Zertifikat der Gegenseite hat. Es
        empfiehlt sich, auf jedem System einen eigenen Schl&uuml;ssel nur f&uuml;r VPNs anzulegen.</p><p>Wenn Sie auf dem Intranator mehrere VPNs einrichten, m&uuml;ssen Sie nicht f&uuml;r jede
        Verbindung extra einen eigenen Schl&uuml;ssel anlegen: Sie k&ouml;nnen einen eigenen Schl&uuml;ssel f&uuml;r
        alle VPNs verwenden. Nur von jeder der Gegenstellen ben&ouml;tigen Sie nat&uuml;rlich den &ouml;ffentlichen
        Schl&uuml;ssel.</p><p>Weitere Details zur Schl&uuml;sselverwaltung finden Sie im <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="vpn-keys.php">39. Kapitel,&nbsp;&#8222;Schl&uuml;sselmanagement&#8220;</a>.</p><p>Eine auf dem Intranator konfigurierte Verbindung gilt f&uuml;r die Verbindung zwischen einem
        Client und einem Netz hinter dem Intranator. M&ouml;chten Sie vom Client auf mehrere Netze hinter
        dem Intranator zugreifen, k&ouml;nnen Sie einfach mehrere Verbindungen konfigurieren. Achten Sie
        darauf, f&uuml;r jede dieser Verbindungen immer dieselbe Kombination an Schl&uuml;sseln/Zertifikaten
        zu verwenden.</p></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="vpn-clients-intranator-basicconfig"></a>40.2.2. Grundeinstellungen</h3></div></div></div><p>Im Men&uuml; Dienste&nbsp;&gt; VPN&nbsp;&gt; Verbindungen k&ouml;nnen Sie
        VPN-Verbindungen im Intranator konfigurieren.</p><p>Auf der ersten Seite stellen Sie die Gegenstelle ein. Die Gegenstelle ist bei einzelnen
        Rechnern &uuml;blicherweise nicht bekannt. Stellen Sie sie daher auf "Dynamische IP (Road
        Warrior)".</p><p>&Uuml;ber das Verschl&uuml;sselungsprofil k&ouml;nnen die verwendeten Verschl&uuml;sselungsalgorithmen
        ausgew&auml;hlt werden; zu Details siehe <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="vpn-basics-algorithms.php">Abschnitt&nbsp;38.5, &#8222;Algorithmen&#8220;</a>. Wichtig ist vor allem, dass die Einstellung f&uuml;r PFS
        (Perfect Forward Secrecy) auf beiden Seiten identisch ist.</p><p>&Uuml;ber die Kapselung wird kontrolliert, wie die Pakete f&uuml;r den VPN-Tunnel eingepackt
        werden. Bei ESP wird die Verschl&uuml;sselung und Authentifizierung in eine H&uuml;lle eingepackt. Bei
        ESP+AH werden Verschl&uuml;sselung und Authentifizierung separat vorgenommen. ESP+AH kann nicht
        durch NAT geleitet werden, daher sollten Sie f&uuml;r einzelne Rechner auf jeden Fall ESP
        verwenden. Diese Einstellung muss auf beiden Seiten der Verbindung identisch sein.</p><div class="mediaobject"><img src="../images/vpn/intranator/client1.png"></div></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="vpn-clients-intranator-auth"></a>40.2.3. Authentifizierung</h3></div></div></div><a class="indexterm" name="vpn-clients-intranator-auth1"></a><a class="indexterm" name="vpn-clients-intranator-auth2"></a><p>W&auml;hlen Sie den eigenen und den Schl&uuml;ssel der Gegenseite aus.</p><div class="mediaobject"><img src="../images/vpn/intranator/client2.png"></div><p>Einige Clientprogramme bieten die M&ouml;glichkeit, zus&auml;tzlich zu einer Authentifizierung per
        Pre-Shared Key (PSK) oder Zertifikaten noch Login und Passwort eines Benutzers zu
        &uuml;berpr&uuml;fen. Dies geschieht mittels des Protokolls <span class="foreignphrase"><em class="foreignphrase">Extended Authentication
          (XAUTH)</em></span>. Soll dies von Clients genutzt werden, aktivieren Sie die Option
          <span class="guimenuitem">XAUTH Servermodus</span>.</p><p>Der XAUTH Servermodus fordert jetzt vom Client dieser Verbindung die Anmeldung mit den
        Daten eines Intranator-Benutzers, der Mitglied einer Gruppe mit dem Recht
          <span class="guimenuitem">Anmeldung am VPN mit XAUTH</span> ist. Dieses Gruppenrecht k&ouml;nnen Sie
        auf der Seite <span class="guimenu"> Benutzermanager&nbsp;&gt; Gruppen&nbsp;:
          Rechte</span> vergeben.</p><p>Wir raten aus den in <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="vpn-basics-limitations.php">Abschnitt&nbsp;38.6, &#8222;Einschr&auml;nkungen&#8220;</a> genannten Gr&uuml;nden davon ab, Verbindungen per
        Pre-Shared Key (PSK) zu authentifizieren. Vor allem bei mehreren mobilen Rechnern ist diese
        L&ouml;sung besonders gef&auml;hrlich.</p><a class="indexterm" name="d0e12166"></a><a class="indexterm" name="d0e12167"></a></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="vpn-clients-intranator-tunnel"></a>40.2.4. Tunnel konfigurieren</h3></div></div></div><p>Auf der Seite "Tunnel" wird konfiguriert, welches Netz durch diese VPN-Verbindung mit
        welcher virtuellen Client-IP verbunden wird.</p><p>&Uuml;ber den Punkt <span class="guimenuitem">Lokales Netz</span> wird das zu verbindende Netz auf
        Seite des Intranators gew&auml;hlt. W&auml;hlen Sie bei der Option <span class="guimenuitem">Lokale
          Netze</span> eines der direkt an den Intranator angeschlossenen oder gerouteten
        Netze aus.</p><p>M&ouml;chten Sie, dass jeglicher Datenverkehr des Clients &uuml;ber den Intranator l&auml;uft und damit
        auch von der Firewall und dem Proxyserver profitiert, stellen Sie bei <span class="guimenuitem">Lokales
          Netz</span> die Option <span class="guimenuitem">Alles (0.0.0.0/0.0.0.0)</span>
        ein.</p><a class="indexterm" name="vpn-clients-intranator-tunnel-virtualip"></a><p>W&auml;hlen Sie bei <span class="guimenuitem">Netz auf Gegenseite</span> den Typ
          <span class="guimenuitem">Freies Netz</span>. W&auml;hlen Sie eine bislang unbenutzte IP, die auch
        nicht in einem der Netze des Intranators oder des Clients liegt. Dies ist die virtuelle IP,
        die Sie auch im Client eintragen m&uuml;ssen. Verwenden Sie immer 255.255.255.255 als
        Netzmaske.</p><a class="indexterm" name="d0e12202"></a><a class="indexterm" name="vpn-clients-intranator-tunnel-modecfg"></a><p>Die meisten VPN-Clients k&ouml;nnen sich ihre virtuelle IP und zugeh&ouml;rigen DNS-Server &uuml;ber die
        Protokollerweiterung <span class="foreignphrase"><em class="foreignphrase">Mode Config</em></span> automatisch zuweisen lassen.
        Wenn ihr Client dies unterst&uuml;tzt (z.B. Shrew Soft, NCP oder iPhone, siehe Beschreibung der
        einzelnen Clients), stellen Sie die Option <span class="guimenuitem">Netz auf Gegenseite</span> auf
          <span class="guimenuitem">IP zuweisen</span> und tragen die IP ein, die der Client bekommen
        soll. Als DNS-Server &uuml;bermittelt der Intranator automatisch seine eigene IP.</p><a class="indexterm" name="d0e12219"></a><div class="mediaobject"><img src="../images/vpn/intranator/client3.png"></div><a class="indexterm" name="vpn-clients-intranator-nat-remoteip"></a><p>Wurde unter <span class="guimenuitem">Lokales Netz</span> ein Netz eingestellt, welches
        Adressen enth&auml;lt, die nicht in lokalen oder anderen VPN-Netzen liegen, kann der Client &uuml;ber
        das VPN aufs Internet zugreifen. Dies gilt insbesondere f&uuml;r die Einstellung
          <span class="guimenuitem">Alles</span>. Da die virtuelle IP normalerweise aus einem privaten
        Adressbereich stammt, kann sie &uuml;ber die Option <span class="guimenuitem">Gegenseiten-IPs bei
          Internetzugriff umschreiben</span> auf die externe Adresse des Intranators
        umgeschrieben werden (NAT). Diese NAT wird nur bei Zugriffen ins Internet aktiv, Zugriffe
        aufs lokale Netz geschehen weiterhin mit der virtuellen IP.</p><p>Die weiteren Optionen der Adressumschreibung (NAT) werden im <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="vpn-nat.php">54. Kapitel,&nbsp;&#8222;L&ouml;sen von IP-Adresskonflikten in VPNs durch NAT&#8220;</a> erkl&auml;rt.</p><a class="indexterm" name="d0e12243"></a></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="vpn-clients-intranator-rights"></a>40.2.5. Rechte</h3></div></div></div><p>In diesem Men&uuml; werden die Rechte des VPN-Clients definiert. Dies betrifft alle Pakete,
        die vom VPN-Client kommen. Eine Beschreibung der Rechteoptionen finden Sie unter <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="base-intranet-rights.php">Abschnitt&nbsp;9.2, &#8222;Zugriffsrechte eines Netzwerkobjekts&#8220;</a>. </p><div class="mediaobject"><img src="../images/vpn/intranator/client4.png"></div></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="vpn-clients-intranator-activation"></a>40.2.6. Aktivierung</h3></div></div></div><p>In diesem Men&uuml; wird konfiguriert, wann die Verbindung aufgebaut und bestehende Sitzungen
        verl&auml;ngert werden. Bei VPN-Clients kann der Intranator selbst die Verbindung nicht
        initiieren. Stellen Sie daher den Start auf <span class="guimenuitem">Passiv / manuell</span> und
        verwenden f&uuml;r die restlichen Optionen die vorgegebenen Werte.</p><div class="mediaobject"><img src="../images/vpn/intranator/client5.png"></div><a class="indexterm" name="d0e12265"></a><a class="indexterm" name="d0e12266"></a></div></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="vpn-clients.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="vpn-clients.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="vpn-ncp-secure-entry.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">40. Kapitel - Anbinden von einzelnen PCs&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;41. Kapitel - VPN mit dem NCP Secure Entry Client</td></tr></table></div>
      
