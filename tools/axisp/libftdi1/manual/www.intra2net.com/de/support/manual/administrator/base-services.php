te der
        unterst&uuml;tzten ISDN-Karten finden Sie unter <a class="ulink" href="../../unterstuetzte_hardware.php" target="_top">http://www.intra2net.com/de/support/unterstuetzte_hardware.php</a>. Es kann nur eine
        ISDN-Karte pro Intranator verbaut werden. Auch bei Karten, die mehrere
          S<sub>0</sub>-Busse anbieten, kann nur einer verwendet werden. Der Intranator
        kann also maximal auf 2 Kan&auml;len gleichzeitig Faxe senden und empfangen. Wird der Intranator
        als virtueller Server verwendet, ist die Faxfunktion prinzipbedingt nicht nutzbar.</p><p>Der Intranator ben&ouml;tigt einen ISDN-Mehrger&auml;teanschluss (<abbr class="abbrev">PTMP</abbr>). Liefert
        die Telefongesellschaft einen solchen, kann dieser direkt genutzt werden. Liefert die
        Telefongesellschaft einen Anlagenanschluss (<abbr class="abbrev">PTP</abbr>), muss die daran
        angeschlossene TK-Anlage einen internen Mehrger&auml;teanschluss f&uuml;r den Intranator
        bereitstellen.</p><p>Um Kompatibilit&auml;tsprobleme zu vermeiden sowie die volle Kapazit&auml;t des Intranators zu
        nutzen, sollte der Intranator an einen eigenen Mehrger&auml;teanschluss an der TK-Anlage
        angeschlossen werden, der nicht mit anderen Ger&auml;ten geteilt wird.</p><p>Unter System&nbsp;&gt;&nbsp;ISDN&nbsp;&gt;&nbsp;Ortsvorwahl
        sollten L&auml;ndercode und Ortsvorwahl eingestellt werden. Muss bei Ihrer TK-Anlage vor jeder
        externen Nummer eine 0 vorgew&auml;hlt werden, kann dies auch hier konfiguriert werden.</p><p>Unter System&nbsp;&gt;&nbsp;ISDN&nbsp;&gt;&nbsp;MSN werden dem
        Intranator die verf&uuml;gbaren Mehrfachrufnummern (<span class="foreignphrase"><em class="foreignphrase">Multiple Subscriber
          Number</em></span> oder <abbr class="abbrev">MSN</abbr>) bekannt gemacht. Einige Telefonanlagen
        begrenzen die Anzahl der m&ouml;glichen MSNs auf 10 St&uuml;ck. Weder der Euro-ISDN-Standard noch der
        Intranator kennen ein solches Limit.</p><div class="tip" style="margin-left: 0.2in; margin-right: 0.5in;"><table border="0" summary="Tip"><tr><td rowspan="2" align="center" valign="top" width="25"><img alt="[Tipp]" src="../images/admon/tip.png"></td><th align="left">Tipp</th></tr><tr><td align="left" valign="top"><p>Sind Sie nicht sicher, auf welche MSNs der angeschlossene
          S<sub>0</sub>-Bus konfiguriert ist, &ouml;ffnen Sie die messages-Logdatei unter
          Information&nbsp;&gt;&nbsp;System&nbsp;&gt;&nbsp;Logdateien im
          Livemodus. Alle eingehenden Anrufe werden dort mit der zugeh&ouml;rigen MSN angezeigt.</p></td></tr></table></div><a class="indexterm" name="d0e4818"></a></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="base-services-fax-receive"></a>16.1.2. Empfang</h3></div></div></div><a class="indexterm" name="base-services-fax-receive1"></a><p>Unter Dienste&nbsp;&gt;&nbsp;Fax&nbsp;&gt;&nbsp;Einstellungen
        wird der Faxempfang grunds&auml;tzlich aktiviert.</p><p>Danach kann unter
        Dienste&nbsp;&gt;&nbsp;Fax&nbsp;&gt;&nbsp;Empf&auml;nger der Faxempfang
        f&uuml;r unterschiedliche Durchwahlen konfiguriert werden. Wird auf einer der dort eingestellten
        MSNs ein Fax empfangen, wird es per E-Mail an den hinterlegten Intranator-Benutzer gesendet.
        Das Fax kann in den Formaten PDF, PNG oder TIFF-G3 an die E-Mail angeh&auml;ngt werden.</p><a class="indexterm" name="d0e4831"></a></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="base-services-fax-send"></a>16.1.3. Versand</h3></div></div></div><div class="sect3" lang="de"><div class="titlepage"><div><div><h4 class="title"><a name="base-services-fax-send-config"></a>16.1.3.1. Einstellungen</h4></div></div></div><a class="indexterm" name="base-services-fax-send1"></a><p>Unter Dienste&nbsp;&gt;&nbsp;Fax&nbsp;&gt;&nbsp;Einstellungen
          wird der Faxversand grunds&auml;tzlich aktiviert. Dort werden auch die abgehende MSN sowie die
          Standardkennung eingestellt.</p><p>Da beim Empfang unterschiedliche Durchwahlen unterst&uuml;tzt werden, macht es nat&uuml;rlich
          auch Sinn, unterschiedliche Kennungen f&uuml;r den Versand zu verwenden. Unter
          Dienste&nbsp;&gt;&nbsp;Fax&nbsp;&gt;&nbsp;Empf&auml;nger kann f&uuml;r jeden
          Empf&auml;nger eine unterschiedliche Kennung eingetragen werden. Diese wird f&uuml;r den Versand
          verwendet, sobald der im Feld <span class="guimenuitem">Name</span> eingetragene Name vom
          Faxprogramm als Benutzername &uuml;bermittelt wird.</p></div><div class="sect3" lang="de"><div class="titlepage"><div><div><h4 class="title"><a name="base-services-fax-send-client"></a>16.1.3.2. Client</h4></div></div></div><a class="indexterm" name="base-services-fax-client1"></a><p>Der Intranator verwendet intern den HylaFax-Faxserver. Zum Versand kann daher jeder
          HylaFax-kompatible Faxclient eingesetzt werden. Eine &Uuml;bersicht &uuml;ber verf&uuml;gbare Faxclients
          bekommen Sie unter <a class="ulink" href="http://www.hylafax.org/content/Desktop_Client_Software" target="_top">http://www.hylafax.org/content/Desktop_Client_Software</a>.</p><p>Auf dem Intranator wird der frei verf&uuml;gbare Client YajHFC mitgeliefert. Er kann im
          Men&uuml; Information&nbsp;&gt;&nbsp;Download
          heruntergeladen werden.</p></div><div class="sect3" lang="de"><div class="titlepage"><div><div><h4 class="title"><a name="base-services-fax-send-yajhfc"></a>16.1.3.3. Installieren und Konfigurieren von YajHFC</h4></div></div></div><p>YajHFC (Yet another Java HylaFAX Client) ist geeignet f&uuml;r Windows (XP bis 8, 32 und
          64 Bit, nicht jedoch Terminal-Server), Mac OS und Linux. Ein fertiges
          Installationsprogramm f&uuml;r Windows ist auf dem Intranator im Men&uuml;
          Information&nbsp;&gt;&nbsp;Download&nbsp;&gt;&nbsp;Software zu
          finden. F&uuml;r alle anderen Betriebssysteme finden Sie passende Versionen unter <a class="ulink" href="http://www.yajhfc.de/" target="_top">http://www.yajhfc.de/</a>.</p><p>Zum Betrieb wird ein Java Runtime Environment (JRE), Version 6 oder neuer, ben&ouml;tigt.
          Sie k&ouml;nnen es von <a class="ulink" href="http://www.java.com" target="_top">http://www.java.com</a> herunterladen.</p><div class="orderedlist"><ol type="1"><li><p>Starten Sie das Installationsprogramm. W&auml;hlen Sie den "RedMon"-Portmonitor, wenn
              Sie beim Installationsprozess danach gefragt werden.</p></li><li><p>Starten Sie YajHFC und &ouml;ffnen das Men&uuml;
                <span class="guimenu">Extras&nbsp;&gt;&nbsp;Optionen</span>.</p></li><li><p>Tragen Sie im Reiter <span class="guimenu">Server Standard</span> den Namen Ihres
              Intranators ein. Stellen Sie den Zeichensatz auf UTF-8. Sie k&ouml;nnen den Benutzernamen
              frei w&auml;hlen, ein Passwort ist nicht erforderlich.</p><p>Wenn Sie als Benutzernamen exakt den Namen verwenden, der im Intranator im Men&uuml;
              Dienste&nbsp;&gt;&nbsp;Fax&nbsp;&gt;&nbsp;Empf&auml;nger im Feld
                <span class="guimenuitem">Name</span> eingetragen ist, werden die dort hinterlegten
              Namens- und Nummernkennungen f&uuml;r den Versand der Faxe verwendet.</p><div class="mediaobject"><img src="../images/base/yajhfc-options1.png"></div></li><li><p>&Ouml;ffnen Sie den Reiter <span class="guimenu">Versand</span> und tragen dort Ihre
              E-Mail-Adresse ein.</p><div class="mediaobject"><img src="../images/base/yajhfc-options2.png"></div></li></ol></div><p>YajHFC ist nun fertig konfiguriert. Zum Versenden von Faxen drucken Sie auf den
          automatisch installierten Drucker "YajHFC Fax". YajHFC startet automatisch und &ouml;ffnet
          einen Dialog zur Eingabe der Faxnummer.</p><div class="mediaobject"><img src="../images/base/yajhfc-send.png"></div><a class="indexterm" name="d0e4913"></a><a class="indexterm" name="d0e4914"></a></div></div><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="base-services-fax-licensing"></a>16.1.4. Unterschiede zwischen den Lizenzen</h3></div></div></div><p>Empfangen und Versenden von Faxen ist nur bei Intranator-Lizenzen m&ouml;glich, die die
