                Festplatten des Rechners &uuml;berschrieben. Stellen Sie daher vor der Installation
                sicher, dass alle Festplatten gefahrlos gel&ouml;scht werden k&ouml;nnen.</p></td></tr></table></div><div class="mediaobject"><img src="../images/install/install-cd.png"></div><p>Wenn die Installation erfolgreich abgeschlossen ist, werden Sie aufgefordert, den
