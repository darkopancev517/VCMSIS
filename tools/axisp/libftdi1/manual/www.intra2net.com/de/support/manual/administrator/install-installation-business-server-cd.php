            Laufwerk. Der Rechner startet danach von der Festplatte den Intranator. Der Intranator
            startet in die Installationskonsole, beschrieben im <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="install-console.php">7. Kapitel,&nbsp;&#8222;Die Konsole&#8220;</a>. </p><a class="indexterm" name="d0e417"></a></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="install-installation-business-server-raid.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="install-installation-business-server.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="install-installation-business-server-compatibility.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">2.5. RAID&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;2.7. L&ouml;sen von Kompatibilit&auml;tsproblemen</td></tr></table></div>
      
