            M&ouml;chten Sie dennoch weitere Software auf der selben Hardware mitnutzen, setzen Sie daf&uuml;r
            eine Virtualisierungsl&ouml;sung ein.</p><p>Starten Sie den Rechner von der beiliegenden Intranator Installations-CD. Sie k&ouml;nnen
