    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">34.2. Rechnerprofile</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="firewall-profiles.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">34. Kapitel - Firewall-Profile</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="firewall-profiles-provider.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="firewall-profiles-client"></a>34.2. Rechnerprofile</h2></div></div></div><p>&Uuml;ber die Option <span class="guimenuitem">Zugriffsberechtigung</span> k&ouml;nnen Sie die
      Grund-Zugriffsrechte festlegen. &#8222;Kein Zugriff&#8220; entspricht den Rechten von &#8222;Basis LAN&#8220; oder
      &#8222;Basis LAN und lokale Netze&#8220;. &#8222;Nur E-Mail&#8220; erlaubt zus&auml;tzlich zu &#8222;Basis LAN&#8220; den Zugriff &uuml;ber
      die E-Mail-Protokolle SMTP, POP3(S) und IMAP(S). &#8222;Nur VPN&#8220; erlaubt zus&auml;tzlich zu &#8222;Basis LAN&#8220;
      den Zugriff auf &uuml;ber den Intranator verbundene VPN-Netze. In diese VPN-Netze ist dann der
      Zugriff mit allen Protokollen m&ouml;glich.</p><p>Die Option <span class="guimenuitem">Webzugriff &uuml;ber Proxy</span> entscheidet dar&uuml;ber, in
      welcher Weise der HTTP- und FTP-Proxy des Intranators genutzt werden kann. &#8222;Freier Zugriff&#8220;
      erm&ouml;glicht den Zugang zum Proxy-Port, erzwingt ihn aber nicht. Erst, wenn der Benutzer den Proxy im
      Browser eingetragen hat, wird er genutzt. &#8222;Proxyzwang&#8220; sorgt daf&uuml;r, dass der direkte Zugriff auf HTTP-Server im
      Internet unterbunden wird. Der Benutzer muss daher den Proxy im Browser eintragen. Bei
      &#8222;Transparenter Proxy&#8220; leitet der Intranator alle Zugriffe auf HTTP-Server f&uuml;r den Benutzer
      unsichtbar auf den Proxy des Intranators um. Daher muss im Browser nichts speziell
      umkonfiguriert werden.</p><p>&Uuml;ber die Option <span class="guimenuitem">Zus&auml;tzliche Dienste</span> k&ouml;nnen weitere Ports (wie
      z.B. HBCI) f&uuml;r den Zugriff ins Internet freigegeben werden.</p><p>Die Option <span class="guimenuitem">Mailtransfer nur &uuml;ber Intranator</span> sorgt daf&uuml;r, dass
      die E-Mail-Protokolle auf den Intranator beschr&auml;nkt werden. Damit wird der Zugriff von
      E-Mail-Programmen direkt auf Mailserver im Internet unterbunden und so sichergestellt, dass alle
      E-Mails &uuml;ber den Intranator laufen m&uuml;ssen. Dies macht Sinn um z.B. daf&uuml;r zu sorgen, dass der
      E-Mail-Virenscanner oder eine Archivierungsfunktion nicht umgangen werden k&ouml;nnen.</p></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="firewall-profiles.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="firewall-profiles.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="firewall-profiles-provider.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">34. Kapitel - Firewall-Profile&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;34.3. Providerprofile</td></tr></table></div>
      
