    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">17.2. Updates</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="base-system.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">17. Kapitel - Systemfunktionen</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="base-system-backup.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="base-system-updates"></a>17.2. Updates</h2></div></div></div><a class="indexterm" name="base-system-updates1"></a><p>Der Intranator enth&auml;lt ein Updatesystem, mit dem er immer auf dem neuesten Softwarestand
      gehalten werden kann. Dies ist n&ouml;tig, um Sicherheitsprobleme schnell beheben zu k&ouml;nnen und den
      Kunden neue Funktionen und M&ouml;glichkeiten zu bieten. Damit &#8222;veraltet&#8220; der Intranator praktisch
      nicht. </p><p>Updates werden grunds&auml;tzlich immer &uuml;bers Internet vom Intra2net-Server heruntergeladen und
      installiert. Manuelles Einspielen von Dateien ist nur f&uuml;r Notf&auml;lle vorgesehen und wird dann
      gemeinsam mit dem Intra2net-Support durchgef&uuml;hrt.</p><p>Unter System&nbsp;&gt;&nbsp;Update&nbsp;&gt;&nbsp;Einstellungen kann das Update
      konfiguriert werden. Der Intranator pr&uuml;ft in der Standardkonfiguration t&auml;glich auf neue
      Versionen und informiert den Administrator. Dieser kann das Update dann entweder sofort oder
      zeitlich versetzt einspielen. Zur Einwahl ins Internet wird (wenn nicht schon online) der
      Standardprovider (siehe <a xmlns:d="http://docbook.org/ns/docbook" xmlns:xlink="http://www.w3.org/1999/xlink" href="base-internet-automatic.php">Abschnitt&nbsp;11.8, &#8222;Verbindungsautomatik&#8220;</a>) verwendet.</p><a class="indexterm" name="base-system-updates-reboot"></a><p>Der Intranator f&uuml;hrt nach jedem Update einen Reboot durch. Werden mehrere Updates
      gleichzeitig installiert, so wird das Update stufenweise von Version zu Version ausgef&uuml;hrt und
      nach jedem Schritt ein Reboot gemacht. Daher kann ein Update &uuml;ber mehrere Versionen eine
      l&auml;ngere Zeit dauern. Schalten Sie den Intranator nicht w&auml;hrend des Updatevorgangs aus!</p><a class="indexterm" name="d0e5070"></a><p>Die aktuelle Version des Intranators kann unter Information&nbsp;&gt;&nbsp;Version
      eingesehen werden.</p><a class="indexterm" name="d0e5073"></a><a class="indexterm" name="base-system-updates-antivirus"></a><p>Das Update der Virendatenbanken wird separat von den Intranator-Updates durchgef&uuml;hrt. In
      der Standardkonfiguration pr&uuml;ft der Intranator st&uuml;ndlich auf neue Virendatenbanken und
      installiert diese dann vollautomatisch.</p><a class="indexterm" name="d0e5081"></a><a class="indexterm" name="base-system-updates-spamfilter"></a><p>Auch die Spammerkmals-Datenbank wird unabh&auml;ngig von den Intranator-Updates durchgef&uuml;hrt.
      Diese wird standardm&auml;&szlig;ig t&auml;glich vollautomatisch aktualisiert.</p><a class="indexterm" name="d0e5089"></a><div class="sect2" lang="de"><div class="titlepage"><div><div><h3 class="title"><a name="base-system-updates-remote"></a>17.2.1. Update-Fernsteuerung via Partnerweb</h3></div></div></div><a class="indexterm" name="base-system-updates-remote1"></a><p>Die Installation von Updates l&auml;sst sich zentral vom Intra2net Partnerweb aus steuern.
        Dies ist f&uuml;r H&auml;ndler mit einer gro&szlig;en Anzahl von Intranatoren sehr bequem. Voraussetzung ist
        die aktivierte Funktion "Update-Fernsteuerung zulassen" unter
        System&nbsp;&gt;&nbsp;Update&nbsp;&gt;&nbsp;Einstellungen. Die Freischaltung im Partnerweb
        erfolgt in ca. 5 Minuten.</p><p>Im Partnerweb w&auml;hlen Sie einzelne Intranatoren aus und k&ouml;nnen dann zu einer gew&uuml;nschten
        Uhrzeit auf die neueste Version updaten. Das Installations-Kommando wird beim n&auml;chsten
        Update-Check &uuml;bermittelt. Liegt die eingestellte Uhrzeit bereits in der Vergangenheit, so
        wird das Update am darauffolgenden Tag installiert.</p><a class="indexterm" name="d0e5102"></a></div></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="base-system.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="base-system.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="base-system-backup.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">17. Kapitel - Systemfunktionen&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;17.3. Backup</td></tr></table></div>
      
