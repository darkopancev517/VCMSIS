    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">52.4. Verbindung</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="vpn-lancom-router-certificate-intranator.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">52. Kapitel - VPN mit Lancom Routern</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="vpn-lancom-router-intranator.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="vpn-lancom-router-connection"></a>52.4. Verbindung</h2></div></div></div><div class="orderedlist"><ol type="1"><li><p>Starten Sie den Setup-Assistenten und w&auml;hlen <span class="guimenuitem">Zwei lokale Netze verbinden
            (VPN)</span>.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-vpn-wizard-select.png"></div></li><li><p>Die VPN-Verbindung soll &uuml;ber eine Internet-Verbindung laufen.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-vpn-wizard-01.png"></div></li><li><p>Der VPN-Typ mu&szlig; bei IPSec belassen werden.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-ssl-vpn.png"></div></li><li><p>Verwenden Sie keinen ISDN-Anschluss, denn daf&uuml;r wird ein Lancom-eigenes Protokoll
          verwendet.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-vpn-wizard-02.png"></div></li><li><p>Die Verbindung wird &uuml;ber feste IP-Adressen oder Dyndns-Namen hergestellt.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-vpn-wizard-03.png"></div></li><li><p>Geben Sie der eigenen Seite sowie der Gegenseite einen Namen. Der Name ist f&uuml;r die
          Verbindung nicht relevant, er muss nur eindeutig sein.</p></li><li><p>Verwenden Sie Zertifikate (RSA) f&uuml;r die Authentifizierung der Verbindung.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-vpn-wizard-06.png"></div></li><li><p>Tragen Sie ein beliebiges Passwort ein. Dieses Passwort wird nicht ben&ouml;tigt, es w&uuml;rde
          nur f&uuml;r das hier nicht verwendete Lancom-eigene ISDN-Protokoll gebraucht werden.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-vpn-wizard-07.png"></div></li><li><p>Geben Sie die Identit&auml;t (=Inhaber) der Zertifikate f&uuml;r die eigene (Lancom) und
          entfernte (Intranator) Seite ein. Die Werte f&uuml;r die Distinguished Names finden Sie z.B.
          auf dem Intranator im Men&uuml; System&nbsp;&gt; Schl&uuml;ssel&nbsp;&gt; Eigene
          Schl&uuml;ssel bzw. Fremde Schl&uuml;ssel, jeweils im Feld <span class="guimenuitem">Inhaber
            (Subject)</span>. Die einzelnen Datenbl&ouml;cke m&uuml;ssen in der umgekehrten Reihenfolge
          wie im Intranator angezeigt eingegeben werden.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-vpn-wizard-08.png"></div></li><li><p>Verwenden Sie den optimierten Verbindungsaufbau (IKE- und PFS-Gruppe 2).</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-vpn-wizard-09.png"></div></li><li><p>Sie k&ouml;nnen die Verbindung entweder st&auml;ndig aufgebaut lassen, oder nur bei Bedarf
          starten. Stellen Sie am besten den Intranator auch entsprechend ein.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-vpn-wizard-10.png"></div></li><li><p>Tragen Sie als <span class="guimenuitem">Gateway</span> die feste IP oder den Dyndns-Namen
          des Intranators ein (im Beispiel <strong class="userinput"><code>meinintranator.dyndns.org</code></strong>). Geben
          Sie dann IP und Netzmaske des Netzes hinter dem Intranator ein, im Beispiel
            <strong class="userinput"><code>192.168.1.0 / 255.255.255.0</code></strong>.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-vpn-wizard-11.png"></div></li><li><p>Der Lancom-Router kann alle IPs des eigenen Netzes per NAT auf eine einzige Adresse
          umschreiben. Dies kann unter Umst&auml;nden helfen, wenn auf beiden Seiten derselbe
          Netzbereich verwendet wird. Lassen Sie diese Funktion im Zweifel deaktiviert.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-vpn-wizard-12.png"></div></li><li><p>Deaktivieren Sie auf jeden Fall die NetBIOS-Option. Sie basiert auf einem propriet&auml;rem
          Lancom-Protokoll und verhindert den Verbindungsaufbau. Sie wird normalerweise nicht mehr ben&ouml;tigt,
          da moderne Windows-Fileserver CIFS &uuml;ber IP verwenden.</p></li><li><p>Schlie&szlig;en Sie den Assistenten ab und starten die Konfiguration des Routers ohne
          Assistenten. Wechseln Sie zu den <span class="guimenu">VPN</span>-Einstellungen, Reiter
            <span class="guisubmenu">IKE-Param.</span> und klicken auf
          <span class="guibutton">IKE-Proposals</span>.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-ike-settings.png"></div></li><li><p>Bearbeiten Sie das IKE-Proposal mit dem Namen
          <span class="guilabel">WIZ-RSA-AES128SHA</span>.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-ike-settings2.png"></div></li><li><p>Tragen Sie bei G&uuml;ltigkeitsdauer einen Wert kleiner als 86400 ein, da dies der
          Maximalwert ist, den der Intranator akzeptiert. Es empfiehlt sich, hier 28800 zu
          verwenden. Denn das entspricht der Standard-Lebensdauer f&uuml;r IKE/Phase 1 von 480 Minuten im
          Intranator.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-ike-settings3.png"></div></li></ol></div></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="vpn-lancom-router-certificate-intranator.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="vpn-lancom-router.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="vpn-lancom-router-intranator.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">52.3. Zertifikat f&uuml;r den Intranator&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;52.5. Intranator</td></tr></table></div>
      
