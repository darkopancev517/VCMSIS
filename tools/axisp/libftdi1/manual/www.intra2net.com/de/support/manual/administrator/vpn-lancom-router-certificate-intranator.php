    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">52.3. Zertifikat f&uuml;r den Intranator</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="vpn-lancom-router-certificates.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">52. Kapitel - VPN mit Lancom Routern</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="vpn-lancom-router-connection.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="vpn-lancom-router-certificate-intranator"></a>52.3. Zertifikat f&uuml;r den Intranator</h2></div></div></div><div class="orderedlist"><ol type="1"><li><p>Der Lancom-Router erfordert eine besondere Konfiguration des Zertifikats auf dem
          Intranator.
          Seit den 8-er Versionen der LCOS-Firmware werden keine selbstsignierten Schl&uuml;ssel mehr
          akzeptiert, sondern nur von einer eigenst&auml;ndigen CA signierte Zertifikate.
          Im folgenden wird gezeigt, wie ein solcher Schl&uuml;ssel auf dem Intranator erzeugt und
          signiert wird.</p></li><li><p>Zun&auml;chst muss das Zertifikat f&uuml;r die CA erstellt werden:
          &Ouml;ffnen Sie im Intranator das Men&uuml; System&nbsp;&gt; Schl&uuml;ssel&nbsp;&gt;
          Eigene Schl&uuml;ssel&nbsp;: Daten.
          Mit einem Klick auf den Men&uuml;punkt Neu&#8220; beginnen Sie die Schl&uuml;sselerstellung.
          Das Zertifikat wird allein zum Signieren der eigentlichen Verschl&uuml;sselungszertifikate
          verwendet, wir nennen es deshalb beispielsweise <strong class="userinput"><code>intranator-ca</code></strong>
          (einzutragen in den Feldern <span class="guimenuitem">Name</span> sowie <span class="guimenuitem">Rechername
            (CN)</span>).</p></li><li><p>Dem Lancom-Router muss nun dieses CA-Zertifikat &uuml;bermittelt werden.
          Dazu exportieren Sie es aus dem Intranator &uuml;ber die Option <span class="guimenuitem">als .pem</span>.
          &Ouml;ffnen Sie dann in <span class="application">LANconfig</span> das Kontextmen&uuml;
          <span class="guimenu">Konfigurations-Verwaltung</span> des entsprechenden Ger&auml;ts.
          W&auml;hlen Sie hier die Option <span class="guimenu">Zertifikat oder Datei hochladen</span>.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-cert-or-file.png"></div><p>
          W&auml;hlen Sie die soeben erzeugte <code class="filename">.pem</code>-Datei aus und laden Sie sie als
          Zertifikattyp <span class="guisubmenu">VPN - Zus&auml;tzliche CA-Zertifikate hinzuf&uuml;gen</span>
          hoch.</p><div class="mediaobject"><img src="../images/vpn/lancom-router/lancom-cert-additional.png"></div></li><li><p>Wenden Sie sich nun wieder dem Intranator zu, unter dem Men&uuml;
          System&nbsp;&gt; Schl&uuml;ssel&nbsp;&gt; Eigene Schl&uuml;ssel&nbsp;: Daten.
          Legen Sie einen weiteren Schl&uuml;ssel als Grundlage f&uuml;r das VPN-Zertifikat an.
          Bei der Erstellung ist zu beachten, dass der Wert des Feldes <span class="guimenuitem">Rechnername
            (CN)</span> (also der Common Name&#8220; eines SSL-Zertifikats)
          wortw&ouml;rtlich sp&auml;ter im Lancom-Router eingetragen wird, ohne Toleranz f&uuml;r Abweichungen.
          Stellen Sie deshalb sicher, dass Ihnen an dieser Stelle kein Tippfehler unterl&auml;uft!</p></li><li><p>Navigieren Sie nun im Intranator zum Men&uuml; System&nbsp;&gt; Schl&uuml;ssel&nbsp;&gt; Eigene
          Schl&uuml;ssel&nbsp;: CA und w&auml;hlen Sie hier den soeben erstellten VPN-Schl&uuml;ssel des
          Intranators aus.
          Im Abschnitt <span class="guimenuitem">Schl&uuml;ssel mit einem anderen Schl&uuml;ssel signieren</span>
          w&auml;hlen Sie den in den vorangehenden Schritten erzeugten CA-Schl&uuml;ssel aus
          (<strong class="userinput"><code>intranator-ca</code></strong>) und klicken anschlie&szlig;end auf
          <span class="guimenuitem">Signieren</span>.</p><p>&Uuml;berpr&uuml;fen Sie nun unter System&nbsp;&gt; Schl&uuml;ssel&nbsp;&gt; Eigene Schl&uuml;ssel&nbsp;:
          Daten den Wert <span class="guimenuitem">Aussteller/CA</span>.
          In diesem sollte die bei der Erstellung des CA-Zertifikats angegebenen Daten
          zusammengefa&szlig;t sein.
          (Wenn Sie obigem Beispiel gefolgt sind, enth&auml;lt dieses Feld die Zeichenkette
          <strong class="userinput"><code>CN=intranator-ca</code></strong>.)
          Der Schl&uuml;ssel kann nun zum Aufbau einer VPN-Verbindung eingesetzt werden.
        </p></li></ol></div></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="vpn-lancom-router-certificates.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="vpn-lancom-router.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="vpn-lancom-router-connection.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">52.2. Zertifikat f&uuml;r das Lancom-Ger&auml;t&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;52.4. Verbindung</td></tr></table></div>
      
