    <div class="navheader"><table width="100%" summary="Navigation header"><tr><th colspan="3" align="center">55.4. Fehler in Phase 2</th></tr><tr><td width="20%" align="left"><a accesskey="p" href="vpn-common-errors-phase1.php">Zur&uuml;ck</a>&nbsp;</td><th width="60%" align="center">55. Kapitel - Fehlerdiagnose</th><td width="20%" align="right">&nbsp;<a accesskey="n" href="appendix.php">Weiter</a></td></tr></table><hr></div><div class="sect1" lang="de"><div class="titlepage"><div><div><h2 class="title" style="clear: both"><a name="vpn-common-errors-phase2"></a>55.4. Fehler in Phase 2</h2></div></div></div><p>In Phase 2 werden die Daten f&uuml;r die IP-Tunnel ausgehandelt. Tritt hier ein Fehler auf, so
      sind meistens falsche IP-Adressen f&uuml;r den Tunnel hinterlegt. Allerdings kann es auch hier
      nicht passende Verschl&uuml;sselungsalgorithmen geben.</p><p>Nicht passende IP-Adressen werden wie folgt protokolliert:
      </p><pre class="programlisting">cannot respond to IPsec SA request because no connection is known for 
    192.168.2.0/24===192.168.1.254[CN=intranator-vpn]...192.168.1.200[CN=client1]</pre><div class="informaltable"><table border="1"><colgroup><col><col></colgroup><tbody><tr><td>
              <p>
                <code class="computeroutput">192.168.2.0/24</code>
              </p>
            </td><td>
              <p>Netz hinter dem Intranator mit dem die Gegenseite die Verbindung aufbauen
              m&ouml;chte</p>
            </td></tr><tr><td>
              <p>
                <code class="computeroutput">192.168.1.254</code>
              </p>
            </td><td>
              <p>IP des Intranators die die Verbindung entgegengenommen hat</p>
            </td></tr><tr><td>
              <p>
                <code class="computeroutput">[CN=intranator-vpn]</code>
              </p>
            </td><td>
              <p>IPSec-ID des Intranators</p>
            </td></tr><tr><td>
              <p>
                <code class="computeroutput">192.168.1.200</code>
              </p>
            </td><td>
              <p>IP der Gegenstelle</p>
            </td></tr><tr><td>
              <p>
                <code class="computeroutput">[CN=client1]</code>
              </p>
            </td><td>
              <p>IPSec-ID der Gegenstelle</p>
            </td></tr></tbody></table></div><p>In diesem Fall wurde beim Client vergessen, die virtuelle IP zu konfigurieren. Das kann
      man daran erkennen, dass hinter der IP des Clients kein Netz mehr angegeben ist. Daher m&ouml;chte
      der Client eine Verbindung mit seiner realen IP statt mit der virtuellen aufbauen (was h&auml;ufig
      wegen NAT fehlschl&auml;gt).</p><p>Ein Verbindungsversuch mit einer falschen virtuellen IP (hier 192.168.2.78) s&auml;he dagegen
      so aus:
      </p><pre class="programlisting">cannot respond to IPsec SA request because no connection is known for 
    192.168.2.0/24===192.168.1.254[CN=intranator-vpn]...
    192.168.1.200[CN=client1]===192.168.2.78/32</pre><p>M&ouml;chte die Gegenstelle eine Verbindung ohne PFS (Perfect Forward Secrecy) aufbauen, auf
      dem Intranator ist es aber aktiviert, sieht das in den Logs so aus:
      </p><pre class="programlisting">we require PFS but Quick I1 SA specifies no GROUP_DESCRIPTION
sending encrypted notification NO_PROPOSAL_CHOSEN to 192.168.1.200:500</pre><p>Auch in Phase 2 m&uuml;ssen die Verschl&uuml;sselungsalgorithmen zusammenpassen. Tun sie dies nicht
      (im Beispiel m&ouml;chte der Client mit einfachem DES verschl&uuml;sseln), sieht dies wie folgt aus:
      </p><pre class="programlisting">IPSec Transform [ESP_DES (64), AUTH_ALGORITHM_HMAC_SHA1] refused due 
    to insecure key_len and enc. alg. not listed in "esp" string
no acceptable Proposal in IPsec SA
sending encrypted notification NO_PROPOSAL_CHOSEN to 192.168.1.200:500</pre><p>Ein erfolgreicher Verbindungsaufbau wird dagegen so protokolliert:
      </p><pre class="programlisting">IPsec SA established</pre><a class="indexterm" name="d0e16512"></a></div><div class="navfooter"><hr><table width="100%" summary="Navigation footer"><tr><td width="40%" align="left"><a accesskey="p" href="vpn-common-errors-phase1.php">Zur&uuml;ck</a>&nbsp;</td><td width="20%" align="center"><a accesskey="u" href="vpn-common-errors.php">Nach oben</a></td><td width="40%" align="right">&nbsp;<a accesskey="n" href="appendix.php">Weiter</a></td></tr><tr><td width="40%" align="left" valign="top">55.3. Fehler in Phase 1&nbsp;</td><td width="20%" align="center"><a accesskey="h" href="index.php">Zum Anfang</a></td><td width="40%" align="right" valign="top">&nbsp;Teil&nbsp;7.&nbsp;Anhang</td></tr></table></div>
      
